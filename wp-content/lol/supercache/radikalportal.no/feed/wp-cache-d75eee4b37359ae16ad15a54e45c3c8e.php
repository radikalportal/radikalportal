den første overgangsperioden er farlig &#8230; Det er ingen garanti for
at det på slutten av treårsperioden vil overgi makta fullstendig
til sivile. &#8230; Dette er ikke forandring. Det er status quo.»</p>



<p>Iranske Hamid
Alizadeh spør i e<a href="https://www.marxist.com/sudan-no-to-a-rotten-compromise.htm?fbclid=IwAR0EYjG3TdW-Y_7SnjI0eqEoz322gotGkbhrWhCwUmud4q7sm9F6noYEmUk">n
kommentar på marxist.com</a> om «hvordan en granskning av disse
hendelsene kan bli uavhengig og transparent når de hovedansvarlige
leder staten og kontrollerer hele politiet, militære og
etterretningen? Hvis politiet og hæren var uavhengige på noen som
helst måte, ville blodbadet vi har sett den siste måneden aldri ha
skjedd. For å illustrere poenget, så nevner ikke overgangsavtalen
at Janjaweed-militsene skal fjernes fra gatene. Militærrådet er en
direkte etterfølger av det gamle regimet. Ved hver eneste korsvei
har det vist at de under ingen omstendigheter vil kompromisse med de
revolusjonære massene, som de ser på som en trussel mot deres
posisjon som herskende klasse.»</p>



<p>Sudans Kommunistiske Parti har <a href="http://solidnet.org/.galleries/documents/Sudanese-Communist-Party-position-on-the-current-situation-in-Sudan.pdf">i en tidligere uttalelse</a> fra begynnelsen av juni erklært at de i utgangspunktet var motstandere av å forhandle med militærrådet om noen annen løsning enn at hele makta umiddelbart skulle overgis til et sivilt styre utgått av Kreftene for frihet og forandring, men at de bøyde seg for flertallet i bevegelsens ønsker om å forhandle fram en kompromissløsning av hensyn til å bevare enheten i opposisjonen. Kommunistene omtalte RSF som «militære gangstere som har terrorisert og myrda tusenvis av uskyldige sivile i Darfur» og Hemeti som «en krigsherre så vel som en krigsforbryter.» I uttalelsen advarer de mot en grov innblanding i Sudans indre anliggender fra fremmede makter og utenlandske selskaper, og sier de at de har sikre kilder på at RSF har mottatt finansiell og muligens militær støtte fra utlandet, samtidig som den regulære sudanske hæren avvæpnes og marginaliseres. Ifølge kommunistpartiet utgjør «planene om å styrke RSF på bekostning av den nasjonale hæren en direkte trussel mot vår fredelige revolusjon og et verktøy for å knuse drømmene til millioner av unge sudanere om et sivilt styresett i landet vårt».</p>



<p>Den
sudansk-darfurske avisen Dabanga <a href="https://www.dabangasudan.org/en/all-news/article/sudan-rebel-movements-denounce-khartoum-agreement">rapporterer
