<p>&#8212; Etter hennes arrestasjon ble familien min henvist til alle mulige offentlige etater for å innhente informasjon om Zara, men alle ignororte oss. Ingen iranske institusjoner ville være ansvarlig for hennes arrestasjon og alle nektet si noe om Zara.  </p>



<p>&#8212; Etter åtte dager uten nyheter om
hennes situasjon, oppdaget vi at Zara ble overført til Sanandaj
fengsel. Da var vi i familien min var i stand til å møte Zara der
en kort stund, sier Kani. 
</p>



<p>Ifølge det østkurdiske nyhetsbyrået Kurdpa.net og Det kurdiske menneskerettighetsnettverket ble Zara arrestert av styrkene fra den iranske etterretningstjenesten 23. mai 2019 og med begrunnelse at hun har undervist morsmål til kurdiske barn.</p>



<p>I det iranske skolevesenet er det ingen opplæring på kurdisk. Det bor 7-8 millioner kurdere i landet.</p>



<p>Zara har en mastergrad i politisk geografi fra Birjand universitet. Den kurdisk sivilaktivisten har undervist kurdisk språk i flere år i byen Sanandaj og i landsbyene rundt. Hun er en av grunnleggerne for sosiokulturelle foreningen Nowzhin som ble grunnlagt i 2013.</p>



<p>Nowzhin har startet sitt virke innenfor iransk lov og har fått offisielt lisens fra iranske myndigheter. Foreningen har hatt stor betydning for morsmålsundervisning i Øst-Kurdistan i byene Sanandaj, Kamyaran, Dehgolan, Saqez og Baneh.</p>



<p>Kurdiske barn fikk gratis tilbud av Nowzhin for å lære det kurdiske alfabetet og morsmålet sitt. I år holdt Nowzhin foreningen en fest i byen Baneh på den internasjonale dagen for morsmål. I tillegg har medlemmene til Nowzhin foreningen plantet frø på treets dag i Øst-Kurdistan.</p>



<p>Zara Mohammadi var også en aktiv
deltaker i en frivillig kampanje som ble dannet av sivile for å
hjelpe etter jordskjelvene i 2017 og 2018 og gjennom denne kampanjen
fikk jordskjelv ofrene mat, vann, telt og klær og andre nødvendige
daglige livs behov.</p>



<p>Folk har rett til å lære sitt eget
morsmål. 
</p>



<p>I ethvert samfunn der folk blir fratatt undervisning på sitt eget morsmål, betyr det at loven i det landet kun er til for én nasjon som hersker over andre nasjoner. Det er en stor urettferdighet og undertrykkelse når det er et språk dominert av andre språk eller en menneskelig gruppe skulle si at deres folk er best over andre menneskelige grupper.</p>



<p>Ifølge Zahra hadde hun vært fengslet
i full isolasjon i fire dager og gått i sultestreik, men på grunn
av alvorlig jernmangel, mistet hun bevisstheten sin fullstendig og
hennes sultestreik førte henne til grensen av koma. 
</p>



<p>Den 18. september 2019 ble Zara overført til avdeling 2 i Sanandajs domstol uten at hun har fått en gang advokat, melder hengaw.net.  </p>



<p>Hennes famille er alvorlig bekymret og
de mener at det er stor sannsynlighet at iranske myndigheter vil
tvinge henne til å tilstå noe har hun aldri gjort, for at Zara skal
få et langvarig fengsel. 
</p>



<p>Zahra Mohammadis søster, Kani Mohammadi, skrev på sin Twitter-side: &laquo;Etter fire måneders ulovlig arrestasjon og uforståelig og uklare siktelsen, ble Zahra ført til retten uten advokat den 18. 9.2019.&raquo;</p>



<p>Nettsteder som er tilknyttet til det
iranske regimet beskylder Zara Mohammadi for medlemskap til et av de
ulovlige østkurdiske opposisjonspartiene.</p>
