<p><strong>Tommy Robinson er gjentatte ganger dømt for vold, svindel og passforfalsking. Nå er han dømt for ringeakt for retten og Per-Willy Amundsen vil gi ham asyl i Norge. </strong></p>



<span id="more-35885"></span>



<p>Stortingsrepresentant for FrP og tidligere justisminister, Per-Willy Amundsen, foreslår å gi asyl til den den britiske høyreekstremisten Tommy Robinson. Robinson grunnla den voldelige antimuslimske organisasjonen English Defence League og er tidligere fotballhooligan. Han har kommet på kant med loven og sonet i fengsel flere ganger, både for politisk vold og andre forhold. </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/06/07/tommy-robinson-slar-ned-mann-etter-fotballkamp/">Tommy Robinson slår ned mann etter fotballkamp</a></strong></p>



<p>I dag blei han igjen dømt, denne gangen for ringeakt for retten. Storbritannia har en lov som gjør det forbudt for media å rapportere fra en rekke pågående rettssaker da de frykter at mediedekning kan påvirke en jury i den ene eller andre retning. Dette forbudet har Tommy Robinson gjentatte ganger brutt og slik satt rettsaker i fare da de kan avlyses og annulleres om det sannsynliggjøres at mediedekninga har påvirket rettprosessen. </p>



<p>Slik satte Robinson prosessen for å dømme en rekke barneovergripere i fare da han forsøkte å intervjue de tiltalte utafor en rettssak. Lignende overtredelser har Robinson gjort seg skyldig i flere ganger og han blei til slutt dømt for overtredelsene i fjor. Dommerne fant at Robinsons rapportering hadde oppmuntret til selvjustis og utgjort fysisk aggresjon mot tiltalte i saken. </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2018/05/30/voldsdomt-hoyreekstremist-fengslet-igjen-per-willy-amundsen-hevder-det-er-en-ytringsfrihetssak/">Voldsdømt høyreekstremist fengslet igjen &#8212; Per-Willy Amundsen hevder det er en ytringsfrihetssak</a></strong></p>



<p>Robinsons tilhengere møtte opp utafor rettslokalet og <a href="https://www.theguardian.com/uk-news/2019/jul/11/tommy-robinson-given-nine-month-jail-term-for-contempt-of-court">blei voldelige</a> da de kastet flasker på politiet og truet journalister. </p>



<p>Per-Willy Amundsen mener i et facebookinnlegg fra i går at Tommy Robinson er politisk forfulgt og hevder at han sjøl vil bli utsatt for sensur av Facebook grunna støtte til den britiske høyreekstremisten. På bildet i posten har Robinson satt tape over egen munn og tatt bilde av seg sjøl. Spådommen om sensur har så langt ikke slått an.</p>



<figure class="wp-block-image"><img src="https://radikalportal.no/wp-content/uploads/2019/07/Screenshot_2019-07-12-Per-Willy-Amundsen-Startside.png" alt="" class="wp-image-35886" srcset="https://radikalportal.no/wp-content/uploads/2019/07/Screenshot_2019-07-12-Per-Willy-Amundsen-Startside.png 569w, https://radikalportal.no/wp-content/uploads/2019/07/Screenshot_2019-07-12-Per-Willy-Amundsen-Startside-150x148.png 150w, https://radikalportal.no/wp-content/uploads/2019/07/Screenshot_2019-07-12-Per-Willy-Amundsen-Startside-300x297.png 300w" sizes="(max-width: 569px) 100vw, 569px" /><figcaption>Fra Per-Willy Amundsens facebookside.</figcaption></figure>



<p>Robinson har ikke bare vist gjentatt ringeakt for retten. Han har en rekke dommer på seg fra tidligere. Han blei kjent i Storbritannia da han i 2009 grunnla og ledet English Defence League, en voldelig antimuslimsk organisasjon som tiltrakk seg nazister og andre fra ytre høyre. Sjøl har han en periode bak seg i det nyfascistiske partiet British National Party. Det var ikke bare medlemmene i EDL som var voldelige. Robinson fikk dommer for to voldshendelser på seg fra treårsperioden han var leder i organisasjonen, samt for tilfeller av å ha brutt en offentlige orden. Han hadde en tidligere dom fra 2005 for overfall. </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2018/07/16/tommy-robinsons-tilhengere-angrep-fagforeningsleder/">Tommy Robinsons tilhengere angrep fagforeningsleder</a></strong></p>



<p>Seinere prøvde han seg på eiendomssvindel, men blei tatt av politiet og dømt til 18 måneders fengsel for de to tilfellene. Han har også en narkotikadom på seg etter å <a href="https://www.independent.co.uk/news/uk/home-news/tommy-robinson-video-drugs-cocaine-islam-far-right-israel-palestine-edl-a8776991.html">ha innrømmet</a> at han hadde 3,48 gram kokain på seg for videresalg. Ved flere tilfeller har Robinson forsøkt å ta seg inn i USA ved bruk av falsk pass. Hans egentlige navn er Stephen Yaxley-Lennon, men verken det eller hans vanlige pseudonym blei brukt i passet. </p>



<p>Nylig blei Robinson filmet da han slo ned en mann etter en fotballkamp.</p>



<p>&#8212; Det som er åpenbart, er at når han er blitt straffeforfulgt på bakgrunn av juridiske teknikaliteter, er det vanskelig å sette det i sammenheng med noe annet enn hans politiske engasjement, sier tidligere justisminister Amundsen <a href="https://www.dagbladet.no/nyheter/per-willy-amundsen-mener-tommy-robinson-bor-fa-asyl-i-norge/71392576">til Dagbladet</a>. </p>



<p>Amundsens asylutspill har <a href="https://www.nrk.no/norge/per-willy-amundsen-vil-gi-asyl-til-islam-motstanderen-tommy-robinson-1.14623229">blitt avvist</a> av regjeringspartner KrF. </p>
