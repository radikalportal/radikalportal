<p><strong>Hvor langt unna må et barn som drukner være før du som menneske slutter å bry deg, Jøran Kallmyr?</strong> </p>



<span id="more-35890"></span>



<p>Kjære Jøran,<br></p>



<p>Jeg kan fortelle deg hvordan døde barn lukter, men jeg ønsker at du skal få slippe å sovne til den lukten hver kveld.&nbsp;</p>



<p>Jeg kan også fortelle deg at et dødt barn nesten ikke veier noen ting. <br>En falsk redningsvest med et bevisstløst barn med armene og beina hengende ned langs siden som en tøydukke fylt av vann, veier nesten ikke noen ting.</p>



<p>Sammenlignet med en voksen, selvsagt. <br>En livløs, voksen mann med våte klær og en ryggsekk med sine siste eiendeler i livet festet på magen, og våte sko, med fingre som har låst seg i en klo-lignende stilling av døds-kramper mens han tviholder på en femåring som ikke lenger er der – de er dødstunge.&nbsp;</p>



<p>Men barna – lett.&nbsp;</p>



<p>Man trenger bare en ledig arm for å løfte et dødt barn opp av vannet. En arm og enorme mengder styrke.<br></p>



<p>Hver gang jeg forsøker å beskrive lukten av døde barn, blir jeg uvel. På et øyeblikk er jeg tilbake der, på stranden, ute på åpent hav, klissvåt og iskald, fylt av et annet menneskes oppkast og avføring, og med hendene og ansiktet innsmurt av olje og bensin og saltvann.&nbsp;</p>



<p>Noen av dem er forvrengt, som stille skrik, noen er råtne og ser ut som de hører til blant havets sjømonstre, noen er vakre, rolige, glassaktige, ser overhodet ikke døde ut, ser bare ut som om de sover.&nbsp;</p>



<p>Det er alltid de som er tyngst å løfte i land.&nbsp;</p>



<p>De som ser ut som om de sover ser så forbannet levende ut.&nbsp;</p>



<p>Men lukten av angst blandet med død og våt gummi lar seg ikke beskrive.<br>Kanskje jeg ikke kan fortelle deg hvordan døde barn lukter likevel, for når jeg først begynner å beskrive, blir lukten av metan og ammoniakk aldri helt borte.&nbsp;<br></p>



<p>Så jeg begynner å sette ord på det tekniske isteden.</p>



<p>De druknede ser alltid litt forskjellige ut. Avhengig av hvor lenge de har ligget i vannet, avhengig av vannets temperatur, hvor dypt de har sunket før kroppen blåses opp til tre ganger sin egen størrelse og de flyter opp til overflaten igjen.&nbsp;</p>



<p>Hos noen lukker luftveiene seg med en gang munnen blir fylt av sjøvann, og en krampetrekning i svelget gjør at strupelokket lukker luftrøret og det bare kommer små mengder vann inn i lungene.&nbsp;</p>



<p>Noen gaper og svelger og rekker å hige etter pusten i panikk mange ganger før kroppen gir opp å kjempe.&nbsp;<br></p>



<p>Det er bløtvevet på utsiden av kroppen som blir spist først. Fisken går for øynene med en gang. Mange av de druknede mangler øyne allerede etter noen få timer.&nbsp;</p>



<p>Men det er vannet i lungene som får menneskekroppen til å synke. Kroppens egne bakterier utvikler gasser som får kroppen til å flyte opp igjen til overflaten. Det er særlig mageregionen som blir oppblåst, noen lik ser ut som om de har kulemage. Kroppen kan inneholde så mye gass at den flyter opp som en ballong.&nbsp;</p>



<p>Stort sett ligger de med ryggen opp. Armene og beina og hodet er tyngre enn resten av kroppen, derfor ser de ut som om de naturlig bøyer seg framover.&nbsp;</p>



<p>Noen setter seg fast i fjell eller planter på bunnen og kommer aldri opp til overflaten igjen. <br>I løpet av en måned kan et lik på havets bunn være helt oppspist av dyr.</p>



<p>De som flyter opp, er ofte sleipe, de sklir ut av hendene våre og går i oppløsning, og den lukten, den lukten lar seg aldri helt vaske av.&nbsp;</p>



<p>I september 2015 ble den tre år gamle syriske gutten Alan Kurdi selve symbolet på båtflyktninger. Både Alans fem år gamle bror og moren deres druknet sammen med Alan den dagen.&nbsp;</p>



<p>Hele verden gråt for Alan Kurdi, men siden den gang har det druknet over 20.000 mennesker, og de fleste barna som skyller opp på Europas strender eller som ligger viklet inn i tang og tare på havets bunn, ligner ikke en sovende Alan Kurdi.&nbsp;</p>



<p>De aller fleste barn som plukkes opp av vannet, ser ut som noe du ikke kan forestille deg i dine verste mareritt.</p>



<p>I den lille havnebyen Vatera på Lesvos skyldte den 10. mars en hodeløs kropp opp på stranden. Kroppen tilhørte ni år gamle Roya, hun hadde falt ut av båten på veien mellom Tyrkia og Lesvos en måned tidligere. Mens familien hennes fortvilet og hjelpeløse satt igjen i en synkende gummibåt fikk de karret seg i land, uten å kunne redde jenta si fra bølgene.&nbsp;</p>



<p>Hodet til Roya har sannsynligvis kommet under propellen på båten eller blitt slått så hardt mot de skarpe klippene rundt kysten at den lille kroppen ikke klarte å holde seg hel lenger, selv ikke i havet, der vi fleste flyter så lette og vektløse om sommeren. Hodet til Roya har vi aldri funnet.&nbsp;</p>



<p>Det er ikke uvanlig, vet du det Jøran? At barna mangler et lem, et bein, en arm, et hode. <br>Kroppsdelene sitter så skjørt og løst på de bittesmå kroppene, det skal ikke mer enn fem harde bølger mot klippene ved en turiststrand, så er barnet slått til det ugjenkjennelige.&nbsp;</p>



<p>Den levende Roya var en vakker, spe afghansk pike med sjenert blikk og lyst rødt sjal. <br>Den døde piken en måned etter &#8230; vel, som jeg skrev, de aller fleste barn som plukkes opp av vannet, ser ut som noe du ikke kan forestille deg i dine verste mareritt.<br></p>



<p>Derfor spør jeg til det kjedsommelige:<br>Hvor langt unna må et barn som drukner være før du som menneske slutter å bry deg?</p>



<p>To meter? To hundre meter? Eller slutter du å bry deg når barnet som drukner, befinner seg to hundre mil unna?&nbsp;</p>



<p>For det drukner fortsatt barn i Middelhavet nesten hver dag og det ser ikke ut til at du som menneske bryr deg!<br></p>



<p>I år er det 65 år siden Flyktningkonvensjonen, som ble utarbeidet av FN i kjølvannet av andre verdenskrig, trådte i kraft.&nbsp;</p>



<p>Flyktningkonvensjonen skulle gjelde for alle – uansett når de flyktet og hvor de flyktet fra. Over et halvt århundre senere ser det ikke ut til at det som skulle være et velfungerende system, fungerer i praksis.&nbsp;</p>



<p>Sommeren i fjor var den dødeligste sommeren i Middelhavet på mange år. I løpet av juni og juli var det bekreftet druknet 1363 mennesker.&nbsp;</p>



<p>På samme tid lå alle redningsskip i Middelhavet i arrest i ulike havner i Sør-Europa klare for redningsoppdrag, med frivillige, tolker og leger om bord hele sommeren. Jeg var jevnlig ombord i både Sea-Eye, Sea-Watch og Lifeline hele sommeren 2018. Nesten til enhver tid visste redningsmannskap hvor de druknede befant seg.</p>



<p>Systematisk har EU forsøkt å stanse alle som redder flyktninger på havet. <br>Gjennom å stenge havnene i Sør-Europa og ved å nekte redningsskipene å legge fra eller til, er det laget en maritim sjøgrense som skal gjøre vår del av Europa ugjennomtrengelig.&nbsp;</p>



<p>Samtidig blir de frivillige og kapteinene om bord i redningsskipene anklaget for menneskesmugling.&nbsp;<br></p>



<p>I 2007 druknet det 53 mennesker utenfor en liten vik i mitt andre hjemland, Malta. Vi som var der, plukket forbrente kropper og mennesker fylt av oljesøl opp av vannet. I løpet av 2009 druknet det nesten 1000 mennesker i samme område, og i løpet av de følgende to årene druknet det tusenvis.&nbsp;</p>



<p>Vet du at lyden av tre hundre mennesker som drukner samtidig høres akkurat ut som måkeskrik utenfor Drøbak en lun sommerdag? Jeg vet det, for jeg veksler mellom å bo i mine to havner, Drøbak, i Norge, og Marsaskala i Malta. Og lyden, lyden – den er helt lik.&nbsp;</p>



<p>Mens frivillige fra Malta og Italia plukket opp døde barn i hundretalls i 2013, levde nordmenn stort sett lykkelig uvitende om hvordan døde barn lukter.&nbsp;</p>



<p>Sakte men sikkert forvandler Middelhavet seg til en massegrav og de vakre små kalksteinsøyene Malta og Lampedusa minner om små gravstøtter.&nbsp;</p>



<p>På ett år reddet Italia alene 150.000 mennesker fra å drukne i månedene oktober 2013 til oktober 2014, ikke et eneste europeisk land ville bistå.&nbsp;</p>



<p>Det var først da Italia ikke hadde råd til å fortsette med redningsoperasjonen Operasjon Mare Nostrum at Operasjon Triton ble iverksatt, og først da sa Norge ja til å sende redningsskip, fordi Triton primært ble en grenseoperasjon og ikke en redningsoperasjon. </p>



<p>Våre skip ville kun plukke opp de som tilkalte hjelp i europeisk farvann, og ikke sette ut i libysk farvann for å søke etter mennesker i havsnød slik de frivillige skipene gjør nå.&nbsp;</p>



<p>Da Norge og andre europeiske land begynte å bidra i Middelhavet. gikk drukningsdødene opp med svimlende 1600% og ble sterkt kritisert av Amnesty International.&nbsp;</p>



<p>”Løfter fra flere europeiske land om å bidra med ressurser, skip og fly til søk – og redningsoperasjonen i Middelhavet ser fint ut på papiret, men så lenge man ikke utvider Tritons operasjonsområde blir dette bare hule løfter”&nbsp; sa Amnesty International 23 april 2015.</p>



<p>Jeg vet ikke om jeg vil påstå at det var så vellykket, Jøran, så kanskje vi kan la de som kan dette, og som påtar seg drittjobben for oss gjennom frivillige organisasjoner bare få fortsette med det, uten at du kritiserer dem?<br></p>



<p>I den lille fiskehavna på Lampedusa sitter den gamle fiskeren Carlos, han har for lengst sluttet å spise tunfisk, og i skuta si har han hatt flere døde enn levende gjester de siste årene, han orker ikke fiske lenger og sitter på en blå trestol og speider utover havet.</p>



<p>Utenfor Tunisia sitter Makthon i en fiskebåt sammen med sønnen sin, men de får flere lik enn fisk i garnet og hver dag må han fortelle sin kone at de heller ikke denne uken har mat nok, de som alltid har kunnet leve på salg av fisk.</p>



<p>På Lesvos sitter en kvinnelig hjelpearbeider som er så traumatisert at hun ikke har dyppet tærne i det skinnende Egeerhavet siden hun plukket opp tusenvis av mennesker fra stranden i 2015.</p>



<p>Selv har jeg sluttet å dykke, og når jeg svømmer i Middelhavet, må jeg stå på føttene til kjæresten min. Jeg, som var en av Maltas mest aktive unge vannpolospillere, klarer akkurat nå ikke bade i Middelhavet uten å speide etter båter og kikke etter kroppsdeler. <br>Det er kanskje ikke så rart når man har sett mennesker drive i land i snart tjue år.&nbsp;</p>



<p>I løpet av de siste tiårene er over 40.000 mennesker bekreftet omkommet på flukt over Middelhavet. Hvor mange som faktisk ligger på bunnen av havet, kommer vi aldri til å få vite. Muligens er det så mye som det tidoble.&nbsp;</p>



<p>Akkurat nå finnes det ingen organisasjoner i eller rundt Middelhavet som dokumenterer hvor mange overfarter som ender med forlis, men så langt i år melder International Organization for Migration at sannsynligheten for å drukne på flukt over den sentrale delen av Middelhavet er tre ganger så høy i 2019 som den var 2018.&nbsp;</p>



<p>Det har med andre ord aldri vært farligere å forsøke å krysse Middelhavet! Og det finnes ikke et eneste bevis for påstander om den såkalte pull-faktoren om at frivillige redningsskip bidrar til at flere setter ut på havet. Verken på libysk, marokkansk eller tyrkisk side. De frivillige organisasjonene har nesten ikke vært ute på havet i år, det er bare den senere tid at de har fått så mye oppmerksomhet at det virker slik.&nbsp;</p>



<p>Mennesker som sitter i skjul i Tyrkias skoger på fjerde måneden og venter på en smugler, eller barn som sitter i torturleire i Libya, forholder seg ikke til om de små skipene Sea-Eye eller Lifeline ligger på havet eller er konfiskert i en havn i Europa. De setter ut på havet når deres tid er inne, når smuglerne sparker og dytter dem ned i skjøre gummiflåter eller trebåter og skyter dem dersom de ikke går ombord, etter å ha betalt mer enn vi betaler for fjorten dager på cruise i Caribien.&nbsp;</p>



<p>Jøran, du skjønner sikkert at ingen setter sine barn i en båt med mindre havet er tryggere enn land, de setter seg i den båten fordi tanken på en europeisk flyktningeleir, og vinter etter vinter i et telt, er bedre enn at dine barn blir bombet ihjel av Taliban på skoleveien, fordi en sint politimann på grensen til Makedonia er tryggere enn fjorten menn mellom beina dine – hver dag &#8211; fordi menn fra Kongo er så voldtatt at vi på Lesvos mangler voksenbleier – for herrer. De setter seg i den båten fordi tolvåringene som går i land på Lampedusa, er så misbrukt at de nesten ikke kan gå, og jentene på femten er gravide etter å ha blitt voldtatt av flere menn enn de kan telle. Kvinner på flukt brenner av seg fingertuppene i teltleirene i Hellas fordi de er så redde for å bli returnert til Tyrkia og det helvete de har rømt fra at de heller lever et liv uten fingeravtrykk og med forbrente fingre.&nbsp;</p>



<p>Noen av de som kommer til de greske øyene nå, har forsøkt å krysse 11 ganger før de kommer seg over havet. Takket være EUs avtale med Tyrkia stopper nå tyrkisk kystvakt ca halvparten av båtene hver natt, det samme gjelder på libysk side.&nbsp;</p>



<p><br>Det er ingen som bare setter seg i en båt og lettvint seiler over. Det er ikke slik det fungerer. <br>Dette er grunnen til at det kommer så få mennesker hit.&nbsp;</p>



<p>Muren langs havet fungerte, men det betyr ikke at færre dør, de bare dør et annet sted.&nbsp;</p>



<p>På Lesvos sitter unge afghanske gutter som snakker flytende norsk fordi dere har deportert dem tilbake til Kabul og de har satt ut på flukt på nytt. Sist jeg var i Moria spilte jeg UNO på norsk, med et barn Norge mener kan få dø alene i Kabul. Jeg er glad han på syvende forsøket klarte å komme seg i en gummibåt som ikke sank, og som endte på Lesvos.&nbsp;</p>



<p>Som du sikkert vet, Jøran, så får mine venner på Watch The Med Alarmphone og Aegean Boat Report hver eneste natt fortvilede nødanrop fra mennesker som drukner, og hver gang forsøker de å tilkalle hjelp.&nbsp;</p>



<p>Hvem er det du mener skal redde dem?</p>



<p>Libysk kystvakt svarer ikke på nød-anrop, og Libya er ikke en trygg havn, italiensk kystvakt vil ikke plukke opp flere enn de absolutt må for å ikke helt miste ansikt, maltesisk kystvakt er underbemannede og utslitte, og øyas kapasitet er sprengt etter tjue år med å stå alene i frontlinjen uten støtte fra EU.</p>



<p>Hvem er det du mener skal plukke opp disse menneskene, som uansett er der, om ikke de frivillige, eller private om du vil, organisasjonene?&nbsp;</p>



<p>Hvem skal redde barna som drukner, trekke de døde fostrene som henger ut av kvinnene og registrere alle de som ender på havets bunn?</p>



<p>På den greske øya Lesvos, langt oppe i et fjell i nærheten av borgbyen Molyvos ligger et berg av 300.000 redningsvester. Hver eneste av dem forteller en historie om et menneske som har tatt avgjørelsen om at havet var tryggere enn land, men det mest påfallende er at 90% av alle redningsvestene er falske. Fylt med isopor, papp eller avispapir gir de en falsk trygghet til den som setter ut på reisen, men de blir raskt forvandlet til en dødsfelle. Som du sikkert vet er også de masseproduserte RIB´ene falske.&nbsp;</p>



<p>En RIB er egentlig et av havets tryggeste fartøy, men det fartøyet flyktningene kommer med er laget av syltynn gummi, uten noen lukkekamre og uten annet enn en tynn papplate i bunn. De er konstruert for engangsbruk, akkurat som de lette påhengsmotorene, som gjerne har teksten Yamaha skrevet baklengs.&nbsp;</p>



<p>Det ser så ofte ut som om det bare er menn ombord i båtene, men som du sikkert vet, Jøran, så sitter kvinnene alltid i midten av båten med mennene beskyttende rundt. Og når de er overført til et redningsskip, så får kvinner og barn sitte under dekk mens menn sitter i stekende sol eller storm over dekk.&nbsp;</p>



<p>Jeg regner med, ettersom du er opplyst nok til å uttale deg, at du følger sidene Aegean Boat Report og Budbringeren fra Helvete på Facebook og at du derfor vet at halvparten av de som kommer nå er kvinner og barn. I den siste redningsaksjonen til Sea-Eye, de som fikk gå trygt i land i Malta, var 39 av de 65 ombord barn.&nbsp;</p>



<p>Malta har sagt seg villig til å være en trygg havn dersom Europa er med på å omfordele menneskene de tar i land. Mitt bittelille hjemland, som også er Europas minste og mest tettbebyggede gjør det Norge ikke engang vil ta i.</p>



<p>Det er fullt på kirkegårdene både på Malta, Lampedusa og Lesvos.</p>



<p>&nbsp;<br>Og selv om vi kan skjule oss bak at vi ikke visste at det druknet mennesker i tusentall i Middelhavet for ti år siden, kan vi aldri si at vi ikke visste at det skjer nå.&nbsp;</p>



<p>Jeg løftet en gang en ung kvinne opp av en trebåt, hun var død og barnet hennes hang fortsatt ut av henne og satt fast i navlestrengen, to menneskeliv i ett var tapt, angsten hun opplevde i båten satte i gang en fødsel ingen kunne gjøre noe med.&nbsp;</p>



<p>Du skal være så gjennomført takknemlig for at noen mennesker tar på seg denne jobben, du burde takke og være så ydmyk for at du ikke trenger å sovne med disse bildene i hodet selv. Du burde sende en varm tanke til mine dykkervenner hver eneste natt de må sette ut i mørket og hente opp barn fra bunnen av havet som ingen vil ha.&nbsp;</p>



<p>Det vi har sett, ligner ikke på noe du kan forestille deg i dine verste mareritt, og de fleste druknede barn ser ikke ut som de sover.&nbsp;</p>



<p>Kjære Jøran, så vidt jeg kan huske har ditt parti, stort sett siden dets spede begynnelse, fremmet private initiativ, så hvorfor er dette private initiativet med ett ikke innenfor det ditt parti fremmer lenger?&nbsp;</p>



<p>Er det i frykt for at vi skal fremstå med et snev av medmenneskelighet for mennesker som flykter fra absolutt alt?</p>



<p>I skrivende stund finnes det ikke et eneste redningsskip i Middelhavet.&nbsp;</p>



<p>Nok en gang er skipene konfiskert og kapteinene siktet. Men det kommer ikke til å være færre mennesker som setter ut på havet i natt av den grunn. 58 lik skyldte i land på en strand i Tunisia i forgårs etter at 80 mennesker er savnet fra ett og samme forlis, slik kan vi ikke ha det.&nbsp;<br></p>



<p>Det er du som kan, og bør bruke din gjennomslagskraft til å legge press på de som bestemmer. Så lenge vi ikke klarer å fikse roten til problemet og sørge for at mennesker slipper å rømme fra helvete, så får vi hylle dem som hjelper dem der de er. Særlig dersom det er midt på åpent hav i storm om natten med eget liv som innsats.&nbsp;<br></p>



<p>Barna som ligger på havets bunn, kan vi aldri få tilbake. Men vi må slutte å begå humanitære overgrep som våre barn må be om tilgivelse for om femti år!<br></p>
