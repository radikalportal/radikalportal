<p><strong>Etter å i 30 år ha leda landet sitt fra den ene katastrofen til den andre blei Sudans islamistiske diktator Omar al-Bashir endelig styrta fra makta i vår. Men folkets krav om demokrati har fortsatt å bli møtt med vold og overgrep fra de gamle generalene som klamrer seg til makta. Og bakom buskene lurer sjeiken av Emiratene og kronprinsen av Saudi-Arabia.</strong></p>



<span id="more-35905"></span>



<p>Omar al-Bashir tok makta i Sudan i 1989 i et militærkupp mot den demokratisk valgte regjeringen til Sadiq al-Mahdi. I tillegg til militæret hadde al-Bashir støtte fra den Nasjonale Islamske Fronten (seinere omdøpt til det Nasjonale Kongresspartiet), et islamistisk parti tilknytta Det Muslimske Brorskap. Hovedprioriteringene for det nye regimet var å knuse uavhengighetsbevegelsen i det kristne og animistiske Sør-Sudan, og å tvangsislamisere og tvangsarabifisere det sudanske samfunnet ovenfra.  Utenrikspolitisk orienterte landet seg mot Iran, og fikk støtte til islamiseringsprosessen fra iranske rådgivere. Fra 1991 til 1996 var Sudan hovedkvarter for Osama bin Laden og al-Qaida, noe som førte dem på kant med USA. Lovgivningen har vært basert på en streng tolkning av sharia. <a href="https://www.dabangasudan.org/en/all-news/article/45-000-women-prosecuted-under-public-order-act-sudan-activists">Ifølge Ihsan Fegeiri</a> i den sudanske kvinneorganisasjonen Nei Til Kvinneundertrykking blei 45 000 kvinner anholdt for å kle eller te seg umoralsk bare i løpet av 2016. Av dem blei 15 000 dømt til offentlig pisking.</p>



<p>I 2003 begynte det et væpna opprør i den vestlige regionen Darfur. Opprøret retta seg mot manglende økonomisk utvikling, og diskriminering av ikke-arabere. På grunn av flere år med tørke oppsto det konflikter om dyrkingsområder mellom arabiske nomadestammer og fastboende afrikanere. For å beholde kontrollen bygde al-Bashir-regimet opp en paramilitær arabisk styrke kalt Janjaweed, som begikk grove krigsforbrytelser mot sivile. <a href="https://en.wikipedia.org/wiki/Omar_al-Bashir#War_in_Darfur">Ifølge FN</a> døde det rundt 300 000 mennesker i krigen i Darfur før det blei inngått en fredsavtale mellom staten og de viktigste opprørsgruppene i 2011.  </p>



<p>I 2009 utstedte den internasjonale
straffedomstolen en arrestordre på Omar al-Bashir etter at han blei
dømt in absentia for å være hovedansvarlig for mord, utryddelse,
tortur og voldtekt retta mot afrikanskætta sivile i Darfur. Disse
forbrytelsene blei begått av Janjaweed-militsen under ledelse av
Mohamed Hamdan «Hemeti» Dagalo. Janjaweed blei reorganisert under
navnet Rapid Support Forces (RSF) i 2013, fortsatt som ei paramilitær
gruppe leda av Hemeti, men nå underlagt den nasjonale etterretnings-
og sikkerhetstjenesten NIFF. Etter Omar al-Bashir blei avsatt av et
militærkupp 11. april i år blei Hemeti nestkommanderende i det
styrende militærrådet. Mange analytikere ser på Hemeti som
mektigere enn rådets formelle leder, generalløytnant Abdel Fattah
al-Burhan.</p>



<p>Krigen mellom regimet til al-Bashir og den sørsudanske geriljabevegelsen SPLA tok ifølge FN <a href="https://web.archive.org/web/20041210024759/http://www.refugees.org/news/crisis/sudan.htm">nesten 2 millioner liv</a>. Krigføringen blei først avslutta ved inngåelsen av Naivasha-avtalen i 2005. En av hovedarkitektene bak avtalen var Norges daværende utviklingsminister Hilde Frafjord Johnson. Avtalen innebar blant annet at det blei avholdt en folkeavstemning om Sør-Sudan skulle løsrives fra rest-Sudan, som ifølge offisielle resultater gikk gjennom med 98,83 % stemmer for uavhengighet i februar 2011. To år etter løsrivelsen gjenoppsto krigen i Sør-Sudan i form av en borgerkrig mellom ulike, stammebaserte fraksjoner i SPLA, som per desember 2018 skal ha tatt nærmere 400 000 liv, <a href="https://www.reuters.com/article/us-southsudan-unrest-toll/study-estimates-190000-people-killed-in-south-sudans-civil-war-idUSKCN1M626R">ifølge den London-baserte Skolen for hygiene og tropisk medisin</a>.</p>



<p>Norske politikere og organisasjoner
spilte en viktig pådriverrolle for at Sør-Sudan blei uavhengig og
underlagt det nå internt stridende SPLA, som det snart skulle vise
seg ikke sto tilbake for regimet i Khartoum i brutalitet og
korrupsjon. Det var Norsk Folkehjelp som finansierte det meste av
infrastrukturen i områdene kontrollert av SPLA-geriljaen, og
Folkehjelpa <a href="https://tv.nrk.no/serie/brennpunkt/1999/FFAD12007399">sies
i tillegg</a> å skulle ha frakta våpen og drivstoff til geriljaen.
Det var også norske politikere sammen med de andre Troika-landene
USA og Storbritannia, som pressa regimet i Sudan til gå med på å
åpne for løsrivelsen av Sør-Sudan gjennom inngåelsen av
fredsavtalen i 2005. Al-Bashir skal på det tidspunktet ha frykta å
bli oppført på «Ondskapens akse» og måtte gjøre innrømmelser
for å sikre seg mot den implisitte trusselen mot en amerikansk
invasjon.</p>



<p> SPLAs overtagelse av makta i sør førte imidlertid ikke bare til katastrofale konsekvenser for befolkningen i Sør-Sudan. Også i (rest-)Sudan fikk løsrivelsen av det oljerike sør store konsekvenser, som skapte grunnlaget for dagens revolusjonære situasjon.</p>



<blockquote class="wp-block-quote"><p><strong>Faktaboks</strong></p><p>Sudan: Land i Øst-Afrika med rundt 40 millioner innbyggere. Britisk koloni fram til 1956, siden skiftende perioder med demokratisk valgte regjeringer og perioder med militærstyre. Har siden uavhengigheten vært herja av borgerkrig mellom regjeringen i hovedstaden Khartoum og opprørsbevegelser i underutvikla og undertrykka regioner som Sør-Sudan og Darfur. Etter løsrivelsen av Sør-Sudan i 2011 sogner det overveldende flertallet av innbyggerne til ulike varianter av sunni-islam. 70 prosent er arabere, og de offisielle språkene i landet er arabisk og engelsk. Tilsammen skal det være 114 ulike språk i bruk i Sudan, og mange ulike etniske grupper med afrikansk avstamning.<br> <br>Omar al-Bashir: Statsleder i Sudan fra han tok makta i et militærkupp i 1989 til han blei avsatt i et militærkupp i april 2019, etter langvarige massedemonstrasjoner som krevde regimets avgang.<br></p><p>Det Nasjonale Kongresspartiet: Islamistisk parti tilknytta det Muslimske Brorskap. Etter militærkuppet i 1989 har Kongresspartiet vært det statsbærende partiet i Sudan. Blei avsatt fra makta sammen med al-Bashir i april 2019.<br> <br>Det sudanske folkets frigjøringshær (SPLA): Geriljabevegelse som kjempa mot regimet i Sudan fra 1983 til 2005. Etter løsrivelsen av Sør-Sudan i 2011 splitta den sørlige grenen seg etter etniske skillelinjer i SPLA (regjeringende) og SPLA (opposisjon) og gikk til krig mot hverandre. De nordlige delene av SPLA skilte seg ut til SPLA (nord) og fortsatte krigfen mot regimet i Khartoum i delstatene Blå-Nilen og Sør-Kordofan.<br> <br>Det militære overgangsrådet: Navnet på militærjuntaen som tok makta i Sudan da de avsatte Omar al-Bashir i april 2019. Militærrådets formelle leder er generalløytnant Abdel Fattah al-Burhan. Nestlederen er Mohamed Hamdan «Hemeti» Dagalo.<br> <br>Rapid Support Forces (RSF): Paramilitær gruppe underlagt den sudanske etterretnings- og  sikkerhetstjenesten NIFF. Sprang ut av de arabiske Janjaweed-militsene som begikk krigsforbrytelser mot afrikansættede sivile under konflikten i Darfur, har siden tjent som til å slå ned på opprørsbevegelser sør i Sudan, vært grensevakter mot Libya og deltatt på saudisk side i krigen i Jemen. Skal være hovedsansvarlig for massakren på sivile demonstranter i Khartoum i juni.<br> <br>Mohamed Hamdan «Hemeti» Dagalo: Leder for Rapid Support Forces, tidligere leder av Janjaweed under borgerkrigen i Darfur. Siden april 2011 nestleder i det militære overgangsrådet som har hatt makta etter avsettelsen av Omar al-Bashir. Blir av mange regna som å være den mektigste  mannen i Sudan.<br> <br>Kreftene for frihet og forandring: En allianse av fagforeninger, politiske partier og opprørsbevegelser danna i januar 2019 for å koordinere protestene mot regimet til Omar al-Bashir. Etter avsettelsen av al-Bashir har de fortsatt å demonstrere for at militærrådet må erstattes av et sivilt styre. </p></blockquote>



<p><strong>Økonomiske problemer og innstramninger</strong>Sudans økonomi er, med unntak av en velutvikla medisinproduksjon, lite industrialisert. Landbruket sysselsetter fortsatt 80 prosent av befolkningen og utgjør en tredjedel av landets BNP. Ifølge <a href="https://atlas.media.mit.edu/en/profile/country/sdn/">en oversikt fra MIT</a> er Sudans viktigste eksportvarer i dag gull, råolje og småfe, og deres viktigste importvarer hvete, raffinert olje og sukker. Eksporten går i fremste rekke til Emiratene, Kina, Saudi-Arabia, India og Egypt mens importen kommer fra Kina, Emiratene, India, Saudi-Arabia og Russland. Arbeidsledigheten er på rundt 13 %, men <a href="https://www.theglobaleconomy.com/Sudan/Youth_unemployment/">hele 27 prosent</a> blant unge mellom 15 og 24 år. På <a href="https://en.wikipedia.org/wiki/List_of_countries_by_Human_Development_Index">FNs indeks over menneskelig utvikling</a> kom Sudan på 167. plass av de 189 undersøkte landene i 2017. Sør-Sudan kom på 187. plass.<br><br></p>



<p>Da
Sør-Sudan løsrev seg i 2011 mista Sudan 75 % av oljereservene sine.
Dette <a href="https://www.globalsecurity.org/military/world/sudan/economy.htm">ramma
økonomien svært hardt</a>, fordi oljeeksporten representerte 70 %
av Sudans eksportinntekter og 36% av statsbudsjettet kom fra denne
eksporten. De gjenværende oljereservene er ikke større enn at de
såvidt dekker landets eget forbruk. Problemene etter tapet av
oljeinntektene blir forsterka av en <a href="https://www.statista.com/statistics/727148/inflation-rate-in-sudan/">svært
høy inflasjo</a>n, den altomgripende korrupsjonen og av at <a href="https://allafrica.com/stories/201712270149.html">store
deler av statsbudsjettet</a> går til det militæret. På
Transparency Internationals <a href="https://www.transparency.org/country/SDN">korrupsjonsindeks</a>
ligger Sudan helt nede på 172. plass av de 180 landene de har
undersøkt. De økonomiske problemene har blant annet medført at
<a href="https://www.indexmundi.com/g/g.aspx?c=su&amp;v=94">Sudans
utenlandsgjeld økte</a> fra 36 milliarder dollar i 2010 til 53
milliarder i 2018. Dette har igjen medført krav om nyliberale
«strukturelle reformer» fra IMF og Verdensbanken.</p>



<p>Den utløsende årsaken til demonstrasjonene mot al-Bashir var at de sudanske myndighetene, <a href="https://www.middleeastmonitor.com/20180125-the-imf-role-in-sudans-bread-protests/">etter press fra IMF</a>, kutta i de statlige subsidiene på hvete og drivstoff, som ifølge ulike medier førte til en dobling, tredobling eller sågar femdobling av brødprisene rundt om i landet. I <a href="https://www.japantimes.co.jp/news/2019/05/27/world/raising-bread-prices-led-protests-eventually-ouster-sudan-leader-omar-al-bashir/#.XSWUs_ZuJPY">en artikkel for AFP</a> forteller bakeriarbeideren Fatima Hussein om situasjonen, som fortsatt ikke er løst, slik: «Jeg har syv barn og for å kjøpe brød til dem må jeg bruke 50 sudanske pund om dagen. Jeg har rett og slett ikke så mye penger.» Samtidig blei det fort klart at regimets forsøk på å kvele demonstrasjonene ved å love <a href="https://www.voanews.com/africa/sudan-boosts-flour-subsidies-40-percent-cut-bread-prices">å gjeninnføre noe</a> av subsidiene ikke kunne fjerne det revolusjonære raseriet og håpet om en bedre framtid som brødprotestene hadde utløst. At protestene blei utløst av det helt grunnleggende behovet for å kunne fø sine barn forklarer kanskje også, vel så mye som de undertrykkende sharialovene, hvorfor kvinnene utgjorde et flertall og gikk i frontlinja i demonstrasjonene. Revolusjonens fremste ansikt utad har vært den 22 år gamle arkitektstudenten Alaa Salah, og et av dens kamprop den karakteristiske <a href="https://www.youtube.com/watch?v=7QnNWEnYLYo">ululeringen</a> som arabiske kvinner bruker under skjellsettende begivenheter.</p>



<p>Dette
har viktige historiske paralleller til tidligere revolusjoner. En av
de utløsende årsakene til den franske revolusjonen var
<a href="https://en.wikipedia.org/wiki/Women's_March_on_Versailles">Kvinnemarsjen
til Versailles</a>, hvor kvinnene i Paris som var rasende over mangel
på brød plyndra våpenlagre, marsjerte til Versailles, inntok
palasset og tvang kongen tilbake til Paris der han måtte gjøre både
konstitusjonelle og økonomiske innrømmelser. Også den russiske
februarrevolusjonen blei utløst ved at kvinnene i Petrograd på den
internasjonale kvinnedagen i 1917 <a href="https://en.wikipedia.org/wiki/February_Revolution#Protests">demonstrerte
mot mangel på brød</a>.</p>



<p>Jean
Shaol beskriver den herskende klassen i Sudan slik på <a href="https://www.wsws.org/no/articles/2019/06/17/suda-j17.html">World
Socialist Web Site</a> : «En korrupt klikk som presiderer over et
land der minst 80 prosent av befolkningen på 40 millioner lever på
mindre enn 1 dollar dagen, hvor 5,5 millioner trengte humanitær
bistand i 2018, som er en økning på 700 000 sammenlignet med 2017,
og hvor anslagsvis 2,47 millioner barn lider av akutt underernæring.»</p>



<p>Gunnar
M. Sørbø <a href="https://morgenbladet.no/aktuelt/2019/06/spillet-om-sudan?fbclid=IwAR1ELUecNmgqBgO5a2GK8M3gSHsHk3yKDSy_QBBHnkD7hZ0LAIP-w1E1zuc">har
i Morgenbladet</a> beskrevet den økonomiske modellen i Sudan slik:
«Bashir satt på toppen av en pyramide der det sirkulerte penger,
tjenester, posisjoner, lisenser og kontrakter til regime-lojale
personer. &#8230; Han mistet stadig mer støtte i folket, som så ham
bestyre et korrupt regime som brukte islam til å berike seg selv
mens andre strevde for å overleve.»</p>



<p>De
enorme, landsomfattende demonstrasjonene som begynte i desember 2018
retta seg altså først mot økningen i prisene og den fallende
levestandarden, men gikk snart over til å rette seg mot å kreve
regimets avgang. 11. april blei Bashir avsatt av et militærkupp, og
makta gikk over i hendene til Det Militære Overgangsrådet.
Demonstrasjonene fortsatte likevel med krav om at det nye
militærrådet skulle overgi makta til en sivil regjering som kunne
forberede overgangen til demokrati. Demonstrasjonene var organisert
av <a href="https://en.wikipedia.org/wiki/Sudanese_Professionals_Association">fagforeningen
for leger og lærere</a>. Politisk har opposisjonen vært samla i
Kreftene for frihet og forandring, som i tillegg til nevnte
fagforening også har samla en rekke ulike politiske partier, fra
Sudans Kommunistiske Parti til det sufi-islamske Ummahpartiet. På
grunn av restriksjonene mot opposisjonell politisk virksomhet er
imidlertid alle demokratiske partier organisatorisk svekka i dagens
Sudan, og et av kravene fra demokratibevegelsen har derfor vært at
det <em>ikke </em>må
avholdes valg umiddelbart, men at en må forberede valgene et par år
så det blir mulig å organisere alternativer til de politiske og
militære grupperingene som springer ut av det gamle regimet.</p>



<p>Kreftene for frihet og forandring innleda forhandlinger med det militære overgangsrådet samtidig som de fortsatte med demonstrasjonene for demokrati og et sivilt styre. Konfrontasjonen kulminerte med <a href="https://www.vg.no/nyheter/utenriks/i/4qxo7a/hun-overlevde-sudan-massakren-naa-vil-hun-at-verden-skal-faa-vite-alt">massakren i Khartoum</a> 3. juni, da over 100 mennesker blei drept, 70 voldtatt og flere hundre såra av den Hemeti-leda RSF-militsen. Opposisjonen, som holdt fast på den ikke-voldelige strategien, svarte med å innføre en tre dagers generalstreik fra 9.-11. juni. Militærrådet valgte da å igjen gå inn i forhandlinger, under ledelse av Den afrikanske unionen og Etiopia. Det foreløpige resultatet av dette er en maktfordelingsavtale som blei inngått 5. juli, der militærrådet og Kreftene for frihet og forandring hver for seg skal utpeke til sammen 11 medlemmer til et styrende overgangsråd, under hvilket det også skal dannes en sivil, teknokratisk regjering. <a href="https://www.csmonitor.com/World/Africa/2019/0708/Sudan-International-pressure-enabled-power-sharing-pact">Ifølge Associated Press</a>, som anonymt siterer en sudansk offiser og to egyptiske tjenestemenn, kom denne avtalen i stand etter intenst utenlandske press. USA skal ha lagt et enormt press på Egypt, Saudi-Arabia og Emiratene om å overtale militærrådet til å gå med på en slik maktfordelingsavtale. Det bringer søkelyset på hvor stor innflytelse saudierne og Emiratene har over både det gamle og nye styret i Sudan, og åssen de kommer til å bruke denne innflytelsen.</p>



<p><strong>Saudi-Arabia og Emiratenes kontroll
over Sudan</strong></p>



<p>Saudi-Arabia og de Forente Arabiske Emirater har sterke økonomiske interesser i Sudan ved at de har investert i langsiktige (som regel 99 års) leieavtaler i sudansk landbruksjord – såkalt «landran». Ifølge <a href="http://www.pulitzercenter.org/reporting/land-grabbing-and-its-implications-sudanese-views-scholar">en artikkel i Pulitzer senter</a> har Sudan per mai 2019 leid ut 762 000 hekter landbruksjord til selskaper fra en rekke ulike land, og er i forhandlinger om utleie av ytterligere 3.4 millioner hektar. For Gulflandene er disse investeringene også sikkerhetspolitiske, fordi de handler om matvaresikkerhet. Disse landene i ferd med å tømme grunnvannsreservene sine, og arbeider derfor med å outsource landbruksproduksjonen på en måte som gjør at de kan være garantert å få stabile matvareleveranser. Saudi-Arabia <a href="http://www.aftenposteninnsikt.no/3-mars-2012/matboble-på-bristepunktet">har vedtatt</a> å utfase hele den innenlandske hveteproduksjonen fordi den er for vannkrevende, sjøl om det siste som meldes er at hveteproduksjonen <a href="https://www.world-grain.com/articles/11796-saudi-arabia-grows-wheat-production">har tatt seg noe opp igjen</a> som erstatning for outsourcing av den enda mer vannkrevende fôrplanten luserne. Saudi-Arabia <a href="https://www.nationalgeographic.com/environment/freshwater/saudi-arabia-water-use/">skal ifølge National Geographic</a> allerede ha brukt opp fire femtedeler av grunnvannsreservene sine, og <a href="https://www.arabianbusiness.com/saudi-groundwater-will-run-out-in-13-years--621171.html">i 2016 hevda en vannekspert</a> ved Kong Faisal-universitetet at landet ville gå tom for vann i løpet av 13 år med den daværende utvinningstakten. 95 % av landets vannforbruk gikk da med til jordbruksproduksjon.</p>



<p>Landran rammer
ofte den lokale befolkningen hardt fordi de mister tilgang til jord,
skog og vann de har brukt i generasjoner, men som de ikke har hatt
skjøter på. Mye av matproduksjonen i denne delen av verden har vært
lav-intensiv og halvnomadisk, uten formelle private eiendomsforhold.
De har hatt svært lite å stille opp mot utenlandske selskaper som
har fått beskyttelse fra den sudanske statens korrupte tjenestemenn.
Bloomberg <a href="https://www.bloomberg.com/features/2019-sudan-nile-land-farming/">rapporterte
i mai i år</a> om hvordan det saudi-jemenittiske selskapet Alrawabi
som leier mer enn 80 000 hektar jord i delstaten Nahr-an-Nil har
pumpa opp så mye vann at nabolandsbyenes brønner gikk tomme for
vann, og ørkenen har slukt store deler av landsbyenes gjenværende
dyrkingsområder. I Øst-Jerif skal fem menn som demonstrerte mot et
lokalt landran ha blitt drept av sikkerhetsstyrker, og ei ung jente
skutt to ganger i beinet da hun skulle hente tilbake løpske kameler
som hadde forvilla seg inn på den private plantasjen.</p>



<p>Etter
løsrivelsen av Sør-Sudan har regimet vært nærmest fullstendig
avhengig av økonomisk støtte fra Saudi-Arabia og Emiratene, som i
stor grad har kunnet diktere landets utenrikspolitikk. Etter saudisk
press valgte Khartoum i 2015 å bryte sine diplomatiske forbindelser
med Iran, på tross av at de hadde vært nære allierte i flere tiår.
Sudan har også stilt med kanonføden i form av bakketropper til
Saudi-Arabias folkemorderiske krig i Jemen. Det skal til en hver tid
være over 14 000 sudanske soldater fra Hemetis RSF som slåss på
saudisk side i Jemen, og av dem <a href="https://www.nytimes.com/2018/12/28/world/africa/saudi-sudan-yemen-child-fighters.html">skal
over 20 %</a> være barn.</p>



<p>Allerede 21. april, 10 dager etter avsettelsen av al-Bashir, lovte Saudi-Arabia og Emiratene å støtte det nye militærregimet med 3 milliarder dollar i bistand. 24. mai dro Hemeti til Saudi-Arabia og møtte landets regjerende kronprins Mohammad bin Salman for samtaler. Samtidig dro militærrådets leder al-Burhan på en reise til Egypt og Emiratene. Da de vendte tilbake hadde tonen deres overfor demonstrantene <a href="https://www.aljazeera.com/indepth/opinion/sudan-tiananmen-moment-190606085755217.html">forandra seg dramatisk</a>. Man slutta å snakke om forsoning og dialog, og omtalte i stedet demonstrantene som «en trussel mot nasjonal sikkerhet». Få dager etterpå skjedde massakren i Khartoum. Ifølge en anonym sudansk militærekspert skal angrepet mot demonstrantene ha vært et av hovedtemaene for diskusjonene, og han hevder <a href="https://www.middleeasteye.net/news/sudan-crackdown-saudis-gave-green-light-assault-protesters">overfor Middle East Eye</a> at «uten at [al-Burhan] hadde fått en slik godkjenning ville han aldri ha vært i stand til å begå en slik forbrytelse».</p>



<p>
Det eneste landet som har kunnet konkurrere med Saudi-Arabia og
Emiratene om innflytelse i Sudan er saudiernes nye erkefiende Qatar.
Saudi-Arabia har siden 2017 forsøkt å isolere Qatar diplomatisk og
økonomisk på grunn av Qatars rolle som støttespiller for Det
muslimske brorskap. Saudierne og Brorskapet har vært fiender siden
det egyptiske Brorskapet deltok i å styrte det saudi-allierte
militærregimet til Hosni Mubarak i Egypt, og saudierne etterpå
støtta militærkuppet mot den demokratisk valgte regjeringen til
Brorskapets Mohammed Morsi. I 2014 erklærte Saudi-Arabia Brorskapet
for å være en terrororganisasjon, noe som også er gjeldende vedtak
i Emiratene.</p>



<p><a href="https://www.albawaba.com/business/sudan-turns-down-aid-exchange-cutting-relations-qatar-turkey-1230932">I
desember fortalte</a> den daværende formannen i det sudanske
parlamentets utenrikskomité Mohamed Mustafa al-Dau at «en stat»
hadde tilbudt regimet til al-Bashir et større beløp i økonomisk
bistand mot å bryte forbindelsene til Qatar, Tyrkia og Brorskapet,
noe sudanerne på tross av sine store økonomiske problemer nekta å
gå med på. Reuters har <a href="https://www.reuters.com/article/us-sudan-bashir-fall-specialreport/special-report-abandoned-by-the-uae-sudans-bashir-was-destined-to-fall-idUSKCN1TY0MV">intervjua
anonyme sudanske tjenestemenn</a> som sier at Emiratene på grunn av
dette avbrøt bensinleveransene til det hardt pressa regimet, og
konspirerte med Sudans etterretningssjef Salah Gosh for å få
al-Bashir avsatt. Qatar og deres allierte Tyrkia har inngåtte
avtaler med Khartoum om å <a href="https://www.bbc.com/news/world-africa-48830270">bygge
ut havneområdet Suakin</a> til en verdi av fire milliarder dollar,
noe saudierne og Emiratene frykter kan legge grunnlag for en tyrkisk
militær flåtebase i Rødehavet. Denne avtalen ser nå ut til å stå
i fare for å bli kansellert. Etter avsettelsen av al-Bashir har det
nye militærregimet <a href="http://www.sudantribune.com/spip.php?article67690">nekta
å møte Qatars</a> utenriksminister Mohammed bin Abdulrahman
al-Thani for samtaler, og forbudt den Qatar-baserte nyhetskanalen
Al-Jazeera å fortsette sin virksomhet i Sudan. 
</p>



<p><strong>Norske penger til drapsmilitsen?</strong></p>



<p>Gulfdiktaturene har imidlertid ikke
vært de eneste utenlandske finansieringskildene til Omar al-Bashir.
De seinere årene har landet også mottatt betydelige pengesummer fra
EU. Nærmere bestemt fra EUs <em>Emergency Trust Fund</em> for Afrika.</p>



<p>11-12. november 2015 blei det avholdt et toppmøte i Maltas hovedstad Valletta der europeiske og afrikanske land blei enige om en plan for å hindre afrikansk migrasjon til Europa. Resultatet av møtet var opprettelsen av Emergency Trust Fund. <a href="https://ec.europa.eu/trustfundforafrica/content/homepage_en">Fondets offisielle formål</a> er å fremme lokal økonomisk utvikling, matvaresikkerhet, konflikthåndtering, legale migrasjonsruter og reintegrering av tilbakesendte migranter, samtidig som en bekjemper menneskesmugling. Men store deler av pengene ser også ut til å gå til å betale for å med militære midler hindre flyktninger og andre migranter i å komme seg i nærheten av Europa. Av de 22 % av fondsmidlene som var avsatt til å regulere migrasjon,  gikk bare 3 prosent til å utvikle trygge og regulære migrasjonsveier, mens over halvparten blei brukt på å hindre og kontrollere migrasjon,  <a href="https://www-cdn.oxfam.org/s3fs-public/file_attachments/bp-emergency-for-whom-eutf-africa-migration-151117-summ-en_1.pdf">ifølge en Oxfam-rapport</a> fra 2017.</p>



<p><a href="https://ec.europa.eu/trustfundforafrica/region/horn-africa/sudan">I
skrivende stund oppgir</a> fondet å ha brukt over 217 millioner euro
i Sudan. Sudans gjenytelse har vært å siden 2015 stenge grensa mot
Libya ved hjelp av Hemetis RSF. De skal delvis ha deportert
flyktninger tilbake til hjemlandet der de risikerer forfølgelse og
tortur, delvis ha sendt dem til flyktningleire i Sudan, og delvis
overtatt deler av det lukrative markedet for menneskesmugling sjøl.
I en nederlandsk rapport fra september 2018 blir EU-byråkrater <a href="https://www.middleeasteye.net/news/eu-accused-hiding-links-sudan-armed-groups-migration-funding">i
følge Middle East Eye</a> sitert på at tusenvis av migranter hvert
år blir arrestert av RSF langs Sudans grenser, og arresterte
migranter forteller at de er blitt torturert og tvunget til å betale
løsepenger etter arrestasjonene. Den sudanske forskeren Suliman
Baldo sier til avisen at sjøl om EU ikke direkte har finansiert RSF
så har de sett gjennom fingrene med at Omar al-Bashir brukte RSF til
å utføre oppdraget med å kontrollere grensa. «EU så en annen
vei, de klaga ikke over at RSF deltok i flere episoder med
menneskehandel. &#8230; EU må på en seriøs måte granske hvilke
negative konsekvenser dette har hatt, særlig ved at de har
legitimert en drapsmilits.» Hemeti sjøl <a href="https://www.aljazeera.com/blogs/africa/2017/11/sudan-rsf-unit-accused-abuses-migrants-171117133237654.html">siteres
i Al Jazeera</a> på at RSF har mottatt støtte fra EU, og at
utenlandske representanter har møtt dem for å observere og tilby
trening av militsen. Maren Sæbø <a href="https://www.bistandsaktuelt.no/arkiv-kommentarer/2019/utsyn-maren-sabo-sudan-krigsherre/">skriver
i Bistandsaktuelt</a> at «I realiteten var den notorisk brutale
janjaweed-militsen fra Darfur blitt Europas grenseforsvar.»</p>



<p>Bistandsaktuelt <a href="https://bistandsaktuelt.no/nyheter/2018/irin-feilslatt-eu-flyktningpolitikk/">gjenforteller</a>
den eritreiske flyktningen Yusefs opplevelse av møtet med de
sudanske grensevaktene slik:</p>



<p>«De sudanske grensevaktene tok med
Yusef til byen Dongola i nord, som nå tjener som en uformell
interneringsleir for flyktninger som blir tatt på grensa. I løpet
av den tre dager lange reisen til Dongola døde over 50 mennesker av
mangel på mat, vann og medisinsk hjelp, anslår Yusef. Deres bønn
om hjelp møtte døve ører. &#8216;Vi fortalte dem at våre venner var i
ferd med å dø, at de led og var tørste og sultne. De bryr seg ikke
om deg.&#8217; I Dongola ble Yusef holdt i et svært kompleks sammen med
flere hundre andre mennesker. Åtte menn fra Bangladesh betalte for
seg, og ble umiddelbar løslatt, sammen med flere somaliere og
sudanere. Men eritreerne og etiopierne ble holdt fengslet i en måned.
Yusef sier han så ni mennesker som døde fordi de ikke fikk
medisinsk hjelp»</p>



<p>SV-leder Audun Lysbakken <a href="https://www.stortinget.no/no/Saker-og-publikasjoner/Sporsmal/Skriftlige-sporsmal-og-svar/Skriftlig-sporsmal/?qid=70000">stilte
i oktober 2017</a> spørsmål til regjeringen om Norge er med å
finansiere RSF gjennom våre bidrag til EU-fondet. Utenriksminister
Ine Marie Eriksen Søreide svarte med en rørende tiltro til EUs
pengebruk at «I følge EU går det ingen midler fra EUTF, eller
andre EU-mekanismer, til Rapid Support Forces eller til sudanske
myndigheter.»</p>



<p>14. juni i år <a href="https://www.stortinget.no/no/Saker-og-publikasjoner/Sporsmal/Skriftlige-sporsmal-og-svar/Skriftlig-sporsmal/?qid=76568">stilte
Lysbakken et nytt spørsmål</a> om hvordan Norge reagerer overfor
Sudan på RSFs overgrep. Søreide svarte da at «Norge har tatt opp
med myndighetene i Khartoum at militærrådets handlinger var
uakseptable og understreket behovet for en rask overgang til et
sivilt styre». Søreide understreka også viktigheten av
internasjonal bistand til Sudan for å håndtere den økonomiske
krisen, og at Norge vil bidra med sin del til dette.</p>



<p>Det er åpenbart riktig at Sudan har
behov for økonomisk hjelp, men det er et annet spørsmål er om det
er riktig å gi en slik hjelp før et sivilt overgangsstyre har
kommet på plass, i alle fall om en kan ende opp med å finansiere
Hemetis RSF. Robert Fisk i <em>The Independent</em> <a href="https://www.independent.co.uk/voices/sudan-saudi-arabia-egypt-abdul-fattah-al-burhan-hemeti-dagolo-nile-a8952321.html">har
rapportert at</a> et av slagordene til demokrati-demonstrantene har
vært «Vi vil ikke ha saudiske penger, sjøl om vi blir nødt til å
leve av bønner og falafel».</p>



<p>Så seint som 14. juni i år, altså
etter massakren i Khartoum og før avtalen om opprettelse av en
overgangsregjering, <a href="https://www.regjeringen.no/en/aktuelt/pm_trustfund/id2654592/">la
regjeringen ut en pressemelding </a>om at Norge gir 60 millioner nye
kroner til EU-fondet, noe som totalt bringer de norske bidragene opp
til 200 millioner siden 2015.</p>



<p>Hilde Frafjord Johnson <a href="http://www.verdidebatt.no/innlegg/11752152-sudans-skjebnetime">skriver
i en kommentar</a> om dette i Vårt Land 2. juli at:</p>



<p>«Kommisjonen avviser at fondsmidler er
gått til RSF. Men flere kommandanter har overfor media 
</p>



<p> bekreftet at de har mottatt EU-penger.
Det er uansett ikke tvil om at  operasjonene langs grensen har
bidratt til å styrke Hemedtis posision. Styrkene hans er godt
utstyrt med mengder av våpen og 
</p>



<p> ammunisjon. Nå sier enkelte rapporter
at RSF har snudd, og selv tjener gode penger på smugling og
menneskehandel. Uansett, en full gjennomgang av EUs Trust Fund er
nødvendig, en må vite hvor ressursene har tatt veien. For få uker 
siden betalte Norge nye 60 millioner  kroner &#8230; I denne 
</p>



<p>skjebnetimen må verken Norge eller
andre aktører tro på keiserens nye klær, men  bidra til løsninger
som gir reelle forandringer for Sudan. Bare dette vil bidra til
stabilitet og fred.»</p>



<p><strong>Veien videre for Sudan</strong></p>



<p>5. juli inngikk
militærrådet og opposisjonen altså en avtale om et midlertidig
overgangstyre som skal forberede innføringa av demokrati.
Hovedtrekkene i avtalen går ut på at det skal dannes et styrende
råd bestående av 5 sivile representanter fra Kreftene for frihet og
forandring, fem militære representanter fra det militære
overgangsrådet, og en sivil representant som skal velges av de to
partene i fellesskap. Rådet skal ledes av en representant fra de
militære de første 21 månedene, og deretter av en sivil
representant i 18 måneder, før det så til slutt avholdes valg. Det
skal også gjennomføres en uavhengig granskning av massakren i
Khartoum 3. juni.</p>



<p><br>Det store
spørsmålet er om militæret faktisk kommer til å gi fra seg makta
etter 21 måneder, og om et samlingspresidentskap som inkluderer
krefter som var ansvarlige for massakren vil være villige til å
gjennomføre en seriøs granskning av den.</p>



