<p>Ingen aviser lyktes å få tak i ham for å få en kommentar på innholdet i bladet. Det virket som om redaktøren hadde gått fullstendig under jorden, inntil han 15. august – en drøy måned etter at han hadde satt i gang kontroversen – kommenterte kritikken i <a href="https://www.aftenposten.no/meninger/debatt/i/naOWqL/Uhyrlig-fordreining-om-Folk-er-folk--Bjonnulv-Evenrud?fbclid=IwAR20OzHVkncwUBQZtR6AcUjlBJWJX71HreHz-RL-74sOxaD44JRFFcQw4Eo">Aftenposten</a>. </p>



<p>Tilsvaret til Bjønnulv, som utelukkende kommenterer aftenpostens leder, går i hovedsak ut på å forsøke å «ta» Aftenposten fordi de har misforstått lederen hans. Han har slett ikke fornektet holocaust, han har bare påstått at dødstallene i Auschwitz har blitt nedjustert. Problemet er bare at kilden han bruker for å fastslå dette <em>uten tvil</em> fornekter holocaust, noe Faurisson faktisk gjorde til sin karrierevei.</p>



<p>Han går også langt i å antyde at norsk presse er <em>mot</em> ytringsfrihet. Han spinner også videre på poenget om at man ikke behøver å være enige i synspunktene til FMI, SIAN eller Alliansen for å slippe de til på trykk, selv om ingen – såvidt meg bekjent – har påstått dette.</p>



<p><strong>Fornekter holocaust</strong><br>Jeg mener at i den grad Bjønnulv føler seg misforstått av Aftenpostens leder, er det fullt og helt hans egen skyld. Han har helt rett i at det <em>han</em> skriver i lederen er at dødstallene i Auschwitz – og ikke Holocaust for øvrig – har blitt nedjustert, men hvor får han denne informasjonen fra?</p>



<p>For å underbygge påstanden sin henviser Bjønnulv til verket «The Victories of Revisionism» av Robert Faurisson i sin helhet. Handler dette verket om å påvise at dødstallene i Auschwitz har blitt nedjustert? Nei. Det handler om at holocaust aldri skjedde. I verket, som i sin helhet finnes på nett, kan man <a href="http://robertfaurisson.blogspot.com/2006/12/victories-of-revisionism.html">lese</a>: </p>



<blockquote class="wp-block-quote"><p>«Hitler beordret aldri <strong>drapet </strong>på en person på grunn av personens rase eller religion. Hva gjelder tallet på seks millioner jødiske dødsfall er dette rent oppspinn som aldri har blitt bevist».</p></blockquote>



<p>Boka er for øvrig også dedisert til den tidligere presidenten i Iran Mahmoud Ahmedinejad, ved siden av en rekke kjente høyreekstremister og holocaustfornektere. </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/05/06/resett-og-antisemittisme/">Resett og
antisemittisme﻿</a></strong></p>



<p>Hvorfor henviser Bjønnulv til dette verket? Og, kanskje enda viktigere, hvorfor henviser han til dette verket i sin helhet og antar at han <em>ikke</em> må stå til rette for å støtte verket i sin helhet? Han har åpenbart lest boka, siden han siterer den. Da må han også være klar over innholdet. Han siterer den som fakta om holocaust, uten videre kommentar. Han kunne selvfølgelig lagt inn en kommentar av typen «jeg henviser her til dette verket, men mener utelukkende Faurissons skildring av Auschwitz er riktig», men det gjør han ikke.</p>



<p><strong>Et enkelt poeng</strong><br>Men om han nå <em>hadde</em> presisert at han bare mente Faurissons skildring av Auschwitz var riktig, hadde han <em>allikevel</em> drevet med holocaustfornektelse. I tilsvaret sitt skriver han: </p>



<blockquote class="wp-block-quote"><p>Et sted mellom 1 og 1,6 millioner mennesker mistet livet i Auschwitz. Vårt (enkle) poeng er at dette står i kontrast til hvilke tall norske medier og oppslagsverk tidligere opererte med, som anslo både 2 og 4 millioner ofre i Auschwitz.</p></blockquote>



<p>Det er overhodet ikke dette han sier i lederen. Der skriver han: «[&#8230;] mens dødstallene i Auschwitz i det stille er blitt kraftig nedjustert, og jakten på styringsdokumentene for holocaust er avlyst» og henviser til «The Victories of Revisionism» av Faurisson. Hva sier Faurisson om Auschwitz? Sier han at, til tross for tidligere antakelser, døde bare mellom 1 og 1,6 millioner mennesker der? Nei. Han sier at det <em>ikke eksisterte</em> noen gasskammere der <em>overhodet</em>.</p>



<p>Tallene Bjønnulv opererer med i tilsvaret er helt i tråd med tallene til Store Norske Leksikon. Om det er sånn at dødstallene har blitt nedjustert og alt han ville var å påpeke dette, hadde han ikke behøvd å sitere Faurisson. Han kunne enkelt bare ha sitert en gammel og en nåværende utgave av SNL.</p>



<p>Å sitere Faurisson på den måten Bjønnulv gjør det har bare <em>én </em>hensikt og det er å <em>legitimere holocaustfornektelse</em>. Det er det Bjønnulv gjør og han burde slutte å sno seg unna. Enten kan han innrømme at han har driti seg ut eller innrømme at han er holocaustfornekter.</p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/08/18/rodt-politiker-pa-glattcelle-politiet-bistar-hoyreekstremister/">Rødt-politiker
på glattcelle: &#8212; Politiet bistår høyreekstremister﻿</a></strong></p>



<p><strong>Mener kritikere er mot ytringsfrihet</strong><br>En annen oppsiktsvekkende ting Bjønnulv tar opp i sin kritikk av Aftenposten, er hvordan han selv forklarer hva som har skjedd. Hvordan kan det ha seg at han får kritikk? Hvorfor beskylder folk han for å fornekte Holocaust? Han skriver: «[forklaringen] ligger i at Folk er Folk har gjort noe moralsk forkastelig, nemlig å forsvare ytringsfriheten på prinsipielt nivå.»</p>



<p>Hva er det
han egentlig sier her? Han sier at Aftenposten mener et prinsipielt forsvar av
ytringsfriheten er moralsk forkastelig. Han sier at Aftenposten, på prinsipielt
grunnlag, er <em>mot</em> ytringsfriheten. Bjønnulv
forveksler motforestillinger og kritikk med forakt for ytringsfriheten. </p>



<p>Denne
forestillingen er ikke ny. Man kan kjenne igjen denne typen retorikk fra ytre
høyre. Spesielt SIAN, som ble tilbudt spalteplass i FeF, har blitt eksperter på
denne typen tenkning. Hver gang SIAN har stand, kan man høre talerne fordømme demonstrantene
– som alltid dukker opp – for å hate ytringsfriheten. I SIAN sitt verdensbilde
hater demonstrantene ytringsfriheten og ønsker å fjerne den for alle som er
uenige med dem. </p>



<p>Det denne
tenkemåten fullstendig fjerner er mulighetsrommet for motytringer og kritikk.
Ifølge denne tenkemåten <em>kan en ikke</em> demonstrere
mot noen uten samtidig å være mot ytringsfriheten. Likeledes kan man ikke
kritisere noen, uten å være mot ytringsfriheten. Du må enten være enig i den
ytrede meningen, eller være mot ytringsfriheten. Det eksisterer ikke noe rom
for kritikk. </p>



<p>Jeg mener selvfølgelig ikke at Bjønnulv mener akkurat dette. Men det han at forklarer det som har skjedd ved å hevde at kritikerne mener et prinsipielt forsvar av ytringsfriheten er moralsk forkastelig, viser at han i det minste tenker i disse baner.</p>



<p><strong>Hvem hetses?</strong><br>Problemet med Bjønnulv sitt innbilte ytringsfrihetsforsvar, er at han er fullstendig blind for hets og sjikanering som en trussel mot ytringsfriheten. Det er en kjent sak at kvinner, og spesielt minoritetskvinner, jevnlig utsettes for hets og sjikane dersom de ytrer seg offentlig. Mest kjent er kanskje hetsoffensiven mot Sumaya Jirde Ali igangsatt av Resett, som Bjønnulv valgte å gi penger til på vegne av FeF. Penger som egentlig skulle gå til å hjelpe romfolk. </p>



<p>Men det ser egentlig ikke ut som Bjønnulv mener hets av
minoritetskvinner i den offentlige debatten er et problem overhodet. Han
starter lederen i det aktuelle bladet med å sarkastisk hevde at «vi syntes <em>så </em>synd på utvalgte minoritetskvinner [&#8230;]»,
mens de som <em>egentlig</em> hetses ikke får
noe sympati.</p>



<p>Denne blindheten blir enda klarere når han beskriver Amnestys «21 politiske tiltak mot netthets» som ekstreme sensurplaner. Det virker som om Bjønnulv ikke mener at hets kan begrense ytringsfriheten. Det som begrenser ytringsfriheten er når du kommer med antisemittiske ytringer og deretter forbys å delta på et arrangement som ikke tillater antisemittiske ytringer (ref. Alliansen og Arendalsuka). </p>



<p>Hvis det han virkelig bryr seg om er ytringsfrihet, hvorfor trykket
han da den trusseldømte nynazisten Morten Lorentzen som skrev for FMI? Hvorfor
inviterte han ikke heller Harald Klungtveit, offeret for Lorentzens trusler, til
å skrive? Han kunne for eksempel skrevet om hvordan Lorentzen truet han på
livet på grunn av ytringene hans. </p>



<p>Dersom Bjønnulv ønsker å være den prinsippfaste forsvareren av ytringsfriheten han poserer som, kan han ikke overse hetsproblematikk. Men det er nettopp det han gjør. Han går til og med ett steg videre og avviser hetsproblematikk i sin helhet, kaller tiltak mot hets sensurplaner og bidrar på sin måte til en økning av hets i ytringsrommet, gjennom å slippe til antisemittiske og rasistiske organisasjoner, og henvise til holocaustfornektelse som om det var fakta.</p>



