<p><strong>&#8212; Politiet bistår høyreekstremister</strong><br>Helt fra øyeblikket de sivile politibetjentene henvendte seg til Berntsen handla det om langt mer enn hvordan han sjøl blei forhindra der og da. Han er en veteran på venstresida, både innafor fagforeningsorganisering, partiarbeid og antifascistisk aksjonisme. Han mener torsdagens hendelse ikke kan sees isolert, men at politiet har brukt lignende metoder overfor venstresida i mange år og på den måten lagt til rette for høyreekstremistene i en politisk kamp. </p>



<p>&#8212; Som politiet holder på nå bistår de høyreekstremistene. De vartes opp og beskyttes, men vi må stå bak gjerder, får bøter og blir bortvist. </p>



<p>Berntsen mener kommunens tiltak mot høyreekstremisme er fånyttes så lenge rasister får et frirom i Oslo sentrum.</p>



<p>&#8212; De tiltaksplanene mot høyreekstremisme kan de bare drite i så lenge Breiviks ideologiske støttespillere gis anledning til å spre rasismen sin i Oslo sentrum. </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2018/07/11/politiet-beskytter-nazister-ogsa-nar-det-ikke-er-almedalsveckan/">Politiet beskytter nazister også når det ikke er Almedalsveckan</a></strong></p>



