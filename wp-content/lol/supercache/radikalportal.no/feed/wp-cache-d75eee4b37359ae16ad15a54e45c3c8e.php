i Darfur, Sør-Kordofan og Blå-Nilen, har erklært at de ikke vil
anerkjenne den inngåtte avtalen, og opprørsgruppa Sudans
Frigjøringsbevegelse har omtalt den som et forræderi. Dabanga
rapporterer <a href="https://www.dabangasudan.org/en/all-news/article/sudanese-celebrate-agreement-between-junta-and-opposition">i
en annen artikkel</a> at RSF i enkelte deler av landet har angrepet
demonstranter som feira overgangsavtalen med varselsskudd og
tåregass.</p>



<p>Spørsmålet er
hvilke alternativer demokratibevegelsen har til den inngåtte
avtalen. Fortsatte demonstrasjoner og streiker kan nok true
militærrådet, særlig fordi store deler av hæren kan tenkes å
sympatisere med opposisjonen. Under demonstrasjonene mot Bashir
<a href="https://www.scmp.com/news/world/africa/article/3005526/troops-shield-protesters-sudan-president-omar-al-bashir-faces">beskytta
vanlige soldater</a> og politifolk demonstrantene mot angrep fra
sikkerhetsstyrkene. Men RSF må en anta er fullstendig lojale mot
Hemeti, og de skal <a href="https://www.aljazeera.com/news/2019/06/sudan-rsf-commander-hemeti-190605223433929.html">i
følge estimater</a> telle runbdt 30 000 mann. Dersom det skulle
oppstå en militær konflikt mellom hæren og RSF kan det ende opp
med å eskalere til en borgerkrig der motsetninger mellom sentrum og
periferi, arabere og ikke-arabere, osv. slår ut i full blomst.</p>



<p>Det vil for
utenforstående være vanskelig å vurdere om den beste taktikken for
opposisjonen og demokratisk innstilte soldater er å konfrontere RSF
direkte, eller å underminere dem langsomt ved å legge vedvarende
press på at militæret holder seg til overgangsavtalen og at
demokratibevegelsen gradvis får mer kontroll over staten og den
regulære hæren.</p>



<p>Sikkert er det i
alle fall at både RSF og deres utenlandske støttespillere i
Saudi-Arabia og Emiratene vil gjøre alt som står i deres makt for å
hindre de revolusjonæres mål om et sivilt og demokratisk styre i
Sudan. Det er dypt beklagelig om RSF og Hemetis makt har blitt og
fortsatt blir bygd opp under ved hjelp av direkte eller indirekte
støtte fra EU og Norge.</p>



<p>Men det er heller
ingen tvil om at demonstrasjonene og streikene mot både det gamle og
det nye militærregimet har utløst en revolusjonær energi og
organisasjonserfaring i kampen for et reelt demokrati og utvikling
blant millioner av mennesker i Sudan og deres venner og landsmenn i
utlandet. Med sitt mot og eksempel kan de vise vei til en lysere og
mer rettferdig framtid ikke bare for folket i Sudan, men i hele den
afrikanske og arabiske verden. Et forent folk kan aldri beseires!</p>
