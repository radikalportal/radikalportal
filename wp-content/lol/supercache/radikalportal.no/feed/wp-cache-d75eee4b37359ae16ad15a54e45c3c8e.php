<p><strong>Frankrikes &laquo;feministiske&raquo; prostitusjonslov</strong></p>



<p>I 2016 innførte Frankrike den såkalte nordiske modellen (altså å kriminalisere kjøp av sex og hallikvirksomhet, men ikke sexsalg). Riktignok forbød Frankrike offentlige bordeller rett etter andre verdenskrig, men landet har likevel vært preget av en kulturell romantisering av prostitusjon. Som i Norge var den franske sexkjøpsloven resultat av feministisk grasrotorganisering. Fra 2009 sto over 50 feministiske organisasjoner bak kravet om å kriminalisere prostitusjonskundene, og de fikk etter hvert med seg studentorganisasjoner, menneskerettighetsorganisasjoner og fagforeninger på laget. Også toppolitikere på venstresida sluttet etter hvert opp om å innføre en sexkjøpslov. Kanskje spilte økonomiske argumenter også inn. I 2015 viste nemlig undersøkelsen Prostcost at prostitusjon koster det franske samfunnet 1,6 milliarder euro i året.&nbsp;</p>



<p>Den franske sexkjøpsloven er mer &laquo;feministisk&raquo; enn den norske, i det den i klartekst slår fast at prostitusjon er vold, at den er et hinder for likestilling og et angrep på menneskelig verdighet.&nbsp;</p>



<p>I Frankrike er alle fylker blitt pålagt å ha exit-programmer for prostituerte, og loven slår dessuten fast at sexkjøpere som blir tatt skal sendes på bevisstgjøringskurs. Loven legger også opp til offentlige holdningskampanjer og til at bevisstgjøring om prostitusjon som hinder for likestilling skal inn i skolen.&nbsp;</p>



<p>Sexkjøpsloven har stor oppslutning i Frankrike. En meningsmålings fra Ipsos fra februar 2019 viser at 78 prosent støtter sexkjøpsloven. Støtten er størst blant kvinner (85 prosent) og blant yngre (82 prosent). Bare 22 prosent er imot sexkjøpsloven. Motstanden er størst blant menn (30 prosent).&nbsp;</p>



<p>Svakhetene ved sexkjøpsloven i Frankrike er den samme som i Norge, nemlig at den ikke følges opp skikkelig. Det er høyst varierende hvordan politiet prioriterer loven lokalt, og de bebudede hjelpetiltakene som fylkene er blitt ansvarlige for, er ennå ikke på plass overalt. I de fylkene som faktisk har satt det i gang, har man oppnådd gode resultater, ifølge journalisten Claudine Legardinier fra franske Le Mouvement du Nid, som deltok på Ottars konferanse.</p>



<p>Et stort problem for å få gjennomført loven er president Macrons kuttpolitikk, som har rammet feministiske organisasjoner hardt. Disse organisasjonene skal i prinsippet bidra til bevisstgjøringskurs i skolene, men har ikke økonomiske ressurser til å få det i stand.&nbsp; Med andre ord, de samme problemene vi har i Norge når det gjelder underfinansiering av kvinnebevegelsen.&nbsp;</p>



<p><strong>Virker sexkjøpsloven?&nbsp;</strong></p>



<p>Motstanderne av sexkjøpsloven hevder at den ikke virker, eventuelt at den har ført til mer vold mot prostituerte. Dessverre foreligger det lite nyere forskning på prostitusjon i Norge, ettersom dette er et forskningsfelt med lav prestisje og dermed er underfinansiert. Den mest seriøse forskningen på effekten av sexkjøpsloven er fra 2014, utført på oppdrag fra regjeringen av Vista Analyse. Deres evaluering konkluderte med at sexkjøpsloven virker. Evalueringen legger vekt på følgende punkter:&nbsp;</p>



<ul><li>Sexkjøpsloven har dempet etterspørselen og dermed bidratt til å redusere omfanget av prostitusjon i Norge.&nbsp;</li><li>De økonomiske vilkårene for å drive med prostitusjon i Norge er blitt dårligere. Dempet etterspørsel har ført til realprisfall og redusert inntjening i de fleste markedene.</li></ul>



<ul><li>Håndhevingen av sexkjøpsloven i kombinasjon med håndheving av straffelovens § 202 (hallikparagrafen) og § 224 (lov mot menneskehandel), har bidratt til å gjøre Norge til et mindre attraktivt land for prostitusjonsbasert menneskehandel enn det som ville vært tilfelle uten disse tre lovene.</li><li>Sexkjøpsloven styrker hallikparagrafen og lov mot menneskehandel. Gjennom håndheving av sexkjøpsloven med tilhørende overvåking får politiet informasjon som ellers ikke ville vært tilgjengelig.</li><li>Sexkjøpsloven og hallikparagrafen retter seg mot hver sin side av markedet – etterspørsel og tilbud – og styrker dermed hverandre.&nbsp;&nbsp;</li><li>Det er ingen indikasjoner på at volden mot prostituerte har økt som følge av sexkjøpsloven. Vold, trakassering og ran er en del av prostitusjonsmarkedet nå, og var det også før loven trådte i kraft.&nbsp;&nbsp;</li><li>Sexkjøpsloven har bidratt til å endre holdninger. Særlig har norske unge menn, mer enn eldre, endret holdninger til kjøp av sex etter at loven kom, der unge er mer negative til kjøp av sex enn eldre.&nbsp;</li></ul>



<p>Sexkjøpsloven fungerer som et pedagogisk virkemiddel for å endre atferd, på samme måte som røykeloven har gjort det. Som Ingeborg Rasmussen fra Vista Analyse formulerte det på Ottars konferanse:&nbsp;</p>



<p>&laquo;<em>De fleste personer som kjøper sex er vanlige menn, godt integrert i samfunnet. Sexkjøp er ingen sentral del av livet deres. Man regner den allmennpreventive effekten av straff for å være mindre dersom lovbruddet er dypt integrert i livet og livsstilen til de man straffer. For eksempel når narkotikabrukere straffes for bruk av narkotika eller narkotikarelatert vinningskriminalitet.</em>&raquo;&nbsp;</p>



<p>Vista Analyses evaluering viser også at om sexkjøpsloven ikke hadde vært der, hadde det mest sannsynlig vært flere prostituerte i markedet og flere sexkjøp. Internasjonal forskning viser at jo mer liberale prostitusjonslovene er i et land, desto mer omfattende er menneskehandel med kvinner og prostitusjonen i landet. Redusert fortjeneste og økt risiko bidrar til å gjøre det norske markedet mindre attraktivt enn det ville vært uten dagens lover. Bakmenn følger markedet og tilpasser seg.&nbsp;</p>



<p>Vista Analyse er ikke et feministisk aktivistorgan, men et forskningsinstitutt med evaluering av virkninger av lover som spesialfelt, og deres konklusjoner må anses å være nøytrale. Deres evaluering har betydd mye for at sexkjøpsloven fortsatt består, til tross for at Norge siden 2013 har hatt borgerlig flertall. Høyre, FrP og Venstre, som alle er imot sexkjøpsloven, har ikke brukt sitt flertall for å få den fjernet etter at de kom i regjering. De har forstått at det ville fungert som en invitasjon til internasjonal mafia om å etablere seg i Norge.</p>



<p><strong>Samtidig har ikke politikerne tatt inn over seg at Vistas Analyse også viser at de sosiale tiltakene overfor prostituerte ikke er gode nok. De har heller ikke tatt inn over seg at det er et problem at utlendingsloven brukes for å deportere ofre for menneskehandel, </strong>eller at man ikke har vedtatt lover for å stanse internettbasert hallikvirksomhet, som med en eufemisme gjerne omtales som eskorte-tjenester. Politiet får lite ressurser til å jobbe mot prostitusjon. Kampen mot porno er et ikke-tema for høyresiden, selv om porno og prostitusjon er to sider av samme sak.&nbsp;</p>



<p>Problemet er at de borgerlige politikerne er mest opptatt av å stoppe kriminalitet knyttet til prostitusjon og menneskehandel, de er lite opptatt av de sosialpolitiske virkemidlene overfor prostituerte, og enda mindre av å endre det kvinnesynet som ligger til grunn for prostitusjon.&nbsp;</p>



<p><strong>Hvordan kan tiltak mot prostitusjon konkret se ut?&nbsp;</strong></p>



<p>En mer konsekvent håndheving av sexkjøpsloven innebærer at prostitusjonskjøp faktisk blir straffeforfulgt og at prostitusjonskundene bør sendes på omskoleringskurs der de blir bevisstgjort om hvilke skader de påfører andre, slik det er satt i gang i Frankrike. Myndighetene må sette i gang offentlige kampanjer for å forebygge prostitusjonskjøp. I tillegg til dette mener vi at prostitusjonens menneskelige og samfunnsmessige skadevirkninger må inn som pensum på ungdomsskolen og videregående skole, og at sammenhengene mellom en pornofisert kultur og prostitusjon blir klarlagt og tematisert.&nbsp;&nbsp;&nbsp;</p>



<p>For å hjelpe ofrene ut av prostitusjon er det viktig at det settes i gang hjelpetiltak i alle norske fylker, der de ansatte har spesialkompetanse på prostitusjonsfeltet. Det sosiale arbeidet må være individuelt tilpasset den enkelte prostituertes livssituasjon.&nbsp;</p>



<p>Ifølge den danske sosialarbeideren Hanne Helth, som har lang erfaring med oppsøkende arbeid blant prostituerte i København og som deltok på Ottars konferanse, må hjelpetiltakene stå på to bein; nemlig exit-programmer for de som vil ut av prostitusjon, og veiledning og støtte til dem som er aktive i prostitusjon. Exit-delen må være det beinet man setter fremst i et land som har erkjent at prostitusjon er vold. Mange ønsker nemlig å slutte med prostitusjon, men vet ikke hvordan de skal få det til på egen hånd fordi det er så mange barrierer mot å lykkes med å komme ut. For eksempel hender det at kvinner går tilbake til prostitusjon fordi de har ødelagt arbeidsevne og mangler sosiale nettverk.&nbsp;</p>



<p><strong>Exit-programmer</strong></p>



<p>Individuelt tilpassede hjelpetiltak i exit-programmer bør bestå av flere innsatser rettet mot oppsøkende virksomhet og døgnåpen rådgivning, juridisk, medisinsk, psykologisk og sosial bistand. Dette innebærer blant annet behandling for rusmisbruk, tilbud om bolig, utdanning og arbeid, bostøtte og kontaktpersonordning, fritt leide-ordning og gjeldssanering, oppholdstillatelse og erstatning til ofre for trafficking slik at de ikke skal risikere å bli retraffickert.&nbsp;</p>



<p>Slike exit-program må ha permanent finansiering og være bemannet med folk med spesialkompetanse.&nbsp;</p>



<p>Exit-programmer med mål om å få folk ut av prostitusjon har paradoksalt nok ikke vært prøvd ut i Norge, men i Danmark.&nbsp; I det fireårige prosjektet <em>Exit Prostitution</em>, gjennomført i Danmarks fire største byer, ble prostituerte tilbudt ettårige forløp der det ble tatt utgangspunkt i behovene de selv kom og ba om hjelp til. Halvparten av de prostituerte som prosjektet var i kontakt med – 139 kvinner, 6 menn og 2 transpersoner, 147 i alt – ba om støtte til å komme ut av prostitusjon. Og det i et land som altså ikke har noen sexkjøpslov.&nbsp; <br><br>Resultatene var gode: andelen som opplevde stress og depresjon ble redusert fra 2/3 til 1/3. Deltagerne opplevde en betydelig framgang i sin livssituasjon, blant annet fikk de bedre skikk på boforhold, økonomi og familiære relasjoner. Prostitusjonsfrekvensen falt jo lenger de kom ut i forløpet.&nbsp;</p>



<p>Deltakerne i prosjektet sa at det som hjalp dem var at innsatsen var individuelt tilpasset, at de hadde en fast støtteperson, at det var fleksibilitet i møtefrekvensen, at medarbeiderne hadde spesialkunnskaper om prostitusjon, ikke fordommer mot mennesker som prostituerer seg, og at man kunne være anonym.&nbsp;</p>



<p>Det er altså ikke umulig å bistå folk som vil ut av prostitusjon med gode resultater, det handler om politisk vilje til å prioritere.&nbsp;<br></p>



<p><strong>Konklusjon</strong></p>



