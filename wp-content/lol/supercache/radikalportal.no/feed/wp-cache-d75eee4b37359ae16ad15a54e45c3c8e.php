<p><strong>Heller enn den moralistiske fordømmelsen av enkeltjournalister bør venstresida angripe maktstrukturene som gjør at media gir oss elitenes verdensbilde.</strong></p>



<span id="more-35897"></span>



<p> Hva gjør man ikke for å unngå den symbolske døden, den å bli sensurert eller sparket?               <br></p>



<p>Jeg har nettopp sett John Pilgers nye film “ The war you don’t see “, og er, som vanlig, imponert over Pilger sitt arbeid for å få fram i lyset det som maktfeltet ønsker å skjule. I dette tilfellet de vestlige imperialistiske krigene sin brutalitet. Som “embedded” journalister også hjelper dem med, og dermed rettferdiggjør de samme kriger. </p>



<div class="wp-block-image"><figure class="alignleft"><img src="https://radikalportal.no/wp-content/uploads/2019/07/TheWarYouDontSee.jpg" alt="" class="wp-image-35899" srcset="https://radikalportal.no/wp-content/uploads/2019/07/TheWarYouDontSee.jpg 265w, https://radikalportal.no/wp-content/uploads/2019/07/TheWarYouDontSee-106x150.jpg 106w, https://radikalportal.no/wp-content/uploads/2019/07/TheWarYouDontSee-213x300.jpg 213w" sizes="(max-width: 265px) 100vw, 265px" /><figcaption>Filmplakat for The Wat You Don&#8217;t See av John Pilger.</figcaption></figure></div>



<p>Men det hele blir litt for moralistisk. Da føler man seg jo straks så mye “bedre enn” disse feige journalistene som “ går til sengs med “  &#8212; som uttrykket gjerne blir oversatt med på folkemunne, &#8212; de militære, og ens store solidariske hjerte svulmer, om mulig, enda mere opp.  </p>



<p>Men det hjelper ingen.<br></p>



<p>Det er jo sånn at innen alle felter er det en kamp om begrensede symbolske goder. Avkastningen, de symbolske godene, for de som arbeider innenfor mediene, er slike ting som å komme inn i større aviser, bli ankerkvinner/menn i tv-kanaler, bli redaktører, osv. Om man da ikke vil stille opp på det mediet sier man må stille opp på er det klart det blir sanksjoner, man kan også få sparken. Eller det er andre straffetiltak som settes i verk som ødelegger karrieren. </p>



<p>For mediet selv, om det ønsker å ta avstand fra å delta i ting det ikke liker, kan man bl.a. true med at det da i framtiden ikke får tilgang til kilder fra høyere institusjoner innen maktfeltet, og viktige personer innen det. Og mange andre sanksjoner, og&nbsp;straffetiltak man kan tenke seg.<br></p>



<p>Så det er ikke alltid så lett for folk som arbeider innenfor mediafeltet heller å bevare sin personlige intergritet i samfunn hvor konkurranseforholdene er harde, og du er “in” eller “out “ avhengig av hvor  “politisk korrekt “ du er. I hvertfall ikke når det spilles også med patriotkortet. </p>



<p>Verre er det naturligvis nå når nesten alle mediene er kjøpt opp av storkapitalen, den finansielle og økonomiske eliten, som er selve det overordnede maktfeltet idag, og som setter premissene for hva folk skal få vite, og hva de absolutt ikke må få vite. </p>



<p>Til det anvender man som oftest eufemismer, og som filmen viser, brukes disse av de i de høyeste posisjonene innen mediafeltet til å forfine brutalitetene og å unnskylde og fraskrive seg ansvar.<br></p>



<p>Venstresiden, synes jeg, burde mere forlate moralismen i temaer som disse, og heller analysere maktstrukturene som setter journalistene i en slik klemme mellom på den ene siden egen integritet og prinsipper, og karrieremuligheter og ønsker på den andre. </p>



<p>Dette gjelder forøvrig også innen mange andre felter, som universitetet , det politiske feltet, jura, m.m., hvor det lages “verdensbilder” via kommunikasjon. Verdensbilder som, hvis man blindt underlegger seg maktfeltet, som i dag mere og mere blander seg inn i hva de underliggende feltene skal gjøre og si, ødelegger mere og mere disse sin autonomi. Og vi får falske “ verdensbilder “ som bare tjener interessene til eliten.</p>
