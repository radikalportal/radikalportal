<p><strong>Sexkjøpsloven ble historisk ved at den flyttet ansvaret for prostitusjon fra de prostituerte til kundene. Det er overgriperne som skal straffes.</strong></p>



<span id="more-36018"></span>



<p><em>Teksten er av Trine Rogg Korsvik, Ane Stø og Anne Kalvig, alle fra Kvinnegruppa Ottar.</em></p>



<div class="wp-block-image"><figure class="alignleft is-resized"><img src="https://radikalportal.no/wp-content/uploads/2019/09/ottartrio-1024x331.jpg" alt="" class="wp-image-36022" width="286" height="92" srcset="https://radikalportal.no/wp-content/uploads/2019/09/ottartrio-1024x331.jpg 1024w, https://radikalportal.no/wp-content/uploads/2019/09/ottartrio-150x49.jpg 150w, https://radikalportal.no/wp-content/uploads/2019/09/ottartrio-300x97.jpg 300w, https://radikalportal.no/wp-content/uploads/2019/09/ottartrio-768x248.jpg 768w" sizes="(max-width: 286px) 100vw, 286px" /><figcaption>Artikkelforfatterne.</figcaption></figure></div>



<p>I år er det 10 år siden sexkjøpsloven ble innført i Norge. Loven som kriminaliserer kjøp av seksuelle handlinger, men ikke salg, er historisk unik: Ansvaret for prostitusjon blir flyttet fra de prostituerte til prostitusjonskundene som holder handelen gående. Det er overgriperne som skal straffes.&nbsp;</p>



<p>Kvinnebevegelsen kjempet fram sexkjøpsloven gjennom mange tiår med hardt, idealistisk og politisk arbeid. Men selv om sexkjøpsloven er viktig for å motvirke at menn kjøper sex, fungerer den ikke optimalt. Det er en akutt mangel på hjelpetiltak for de som ønsker seg ut av prostitusjon. Utenlandske ofre for menneskehandel blir deportert, politiet prioriterer ikke å bekjempe prostitusjon, og det er heller ikke iverksatt noen holdningskampanjer eller forebyggende tiltak. Det er høyst oppsiktsvekkende ved innføring av en helt ny lov.&nbsp;</p>



<p>Da Kvinnegruppa Ottar den 15. juni i år inviterte til en dagskonferanse om sexkjøpsloven og kampen som ledet fram til loven, muliggjorde vi et bredt spekter av synspunkt og kampforståelser fra samme scene. Fra erfaringer i Tana i nord på 1990-tallet til dagens Frankrike, fra LOs politikk til tyske feministers framgangsrike kamp mot prostitusjon, fikk vi innsikt i sexkjøpslovens betydning og samfunnsrelevans (se faktaboks). Fra ulike vinkler kastet vi lys over hvordan sexkjøpsloven kom til, hvordan den virker i dag og hva som skal til for at den skal fungere bedre.&nbsp;&nbsp;</p>



<p>Konklusjonen etter konferansen er klar: Kvinnegruppa Ottar krever at politikerne tar ansvar og følger opp sexkjøpsloven med målrettede tiltak!&nbsp;</p>



<p><strong>Hensikten med sexkjøpsloven&nbsp;</strong></p>



<p>Hensikten med sexkjøpsloven er å få slutt på prostitusjon fordi det er farlig, skadelig og fornedrende for dem som prostituerer seg, og en viktig inntektskilde for kriminelle nettverk. Prostitusjonens ideologi er at kvinnekroppen (og yngre menns kropper) er varer som er til salgs, at sex er en rettighet menn har krav på. Prostitusjon påvirker også de som ikke er direkte involvert i prostitusjonshandelen, det vil si hele kulturen og samfunnet der prostitusjon finnes. Dette er altså ikke et marginalt problem for spesielt interesserte, men en diskusjon som går rett inn i hva vi mener skal være rådende verdier og rettigheter, og hvordan samfunnet kan legge til rette for å verne og fremme disse verdiene og rettighetene.&nbsp;</p>



<p>Likevel er det få som bryr seg om skadene kvinner i prostitusjon utsettes for, utenom den organiserte kvinnebevegelsen. I motsetning til kvinnebevegelsens århundrelange, organiserte kamp mot prostitusjon, står organisasjoner som PION (Prostituertes interesseorganisasjon i Norge) FRI og Amnesty, som i dag ønsker å legge til rette for prostitusjon og fjerne sexkjøpsloven. Disse får støtte fra partier på høyresida.</p>



<p><strong>Samfunnets ansvar for å bekjempe og forebygge prostitusjon&nbsp;</strong></p>



<p>Ingen offentlige institusjoner tar virkelig grep for å forebygge og hjelpe folk ut av prostitusjon, som jo faktisk var en av hensiktene med sexkjøpsloven. Dermed må politikere komme på banen og sørge for å få på plass og sikre denne type kompetanse og tilbud.&nbsp;</p>



<p>Prostituerte er ifølge norsk lovverks definisjon utsatt for overgrep og har krav på beskyttelse fra det offentlige. Den norske staten har gjennom lovverket valgt å plassere ansvaret for overgrepet hos den som kjøper og tilrettelegger for prostitusjon, inkludert halliker. Da kan man ikke samtidig ignorere de skadene prostituerte utsettes for, og de akutte og langsiktige behovene for hjelp og rehabilitering som denne gruppa har. Rehabilitering av ofre for prostitusjon innebærer en kostnad, og man må samtidig intensivere innsatsen rettet mot prostitusjonskundene, i all hovedsak menn, og slik hindre at overgrepene oppstår i første omgang. Uten menns etterspørsel etter kropper å få kjøpt, finnes heller ikke prostitusjon, og da heller ikke skadede og traumatiserte kvinner som resultat.&nbsp;</p>



<p><strong>Hvordan kom sexkjøpsloven i stand?&nbsp;</strong></p>



<p>I det følgende viser vi hvordan sexkjøpsloven ble til som historisk, ny norsk lov. Vi ser på hva vi kan lære fra andre lands prostitusjonslovgivning, eksemplifisert ved prostitusjonsliberale Tyskland på den ene siden og Frankrike på den andre siden. I 2016 innførte Frankrike en sexkjøpslov som likner den norske, men som i større grad inkluderer hjelpetiltak og holdningsskapende kampanjer. Det påstås ofte at sexkjøpsloven ikke fungerer, men forskning viser at den faktisk har bidratt til å redusere prostitusjon i Norge. Vi gir også eksempler på hvordan bedre tiltak konkret kan se ut.</p>



<p>Motstandere av sexkjøpsloven hevder gjerne at den bygger på et puritansk og moralistisk syn på sex og arbeid. De fortier at sexkjøpsloven aktivt forholder seg til menneskerettighetene og til en etikk som sier at kropp ikke skal kunne kjøpes.&nbsp; At sexkjøpsloven er resultat av flere tiår med feministisk mobilisering, blir usynliggjort.&nbsp;</p>



<p>Da de feministiske forskerne Cecilie Høigård og Liv Finstad så tidlig som i 1981 foreslo å &laquo;kriminalisere horekundene&raquo; var det grundig forskning og inngående kjennskap til skadene de prostituerte gjennomlever som førte til denne konklusjonen.&nbsp;</p>



<p>Begrepet &laquo;horekunde&raquo; var nytt, og brukt for å flytte stigmaet fra de som selger sex til kjøperne. Aktivister fra kvinnebevegelsen skapte oppmerksomhet om kravet ved å aksjonere i Oslos prostitusjonsstrøk og spraye &laquo;horekunde&raquo; på bilene til menn som prøvde å kjøpe sex av dem. Virkningen var enorm. &laquo;Horekunder jages i Oslo&raquo; sto det på forsiden av avisene. Potensielle kunder våget seg ikke ned på strøket.&nbsp;</p>



<p>I 1982 ble &laquo;kriminalisering av horekunder&raquo; debattert på Stortinget. Men det var ikke flertall for det, fordi de fleste mannlige stortingsrepresentanter (som var i stort flertall) var imot.&nbsp;</p>



<p>Saken lå død i flere år inntil Sovjetunionens sammenbrudd i 1991, med påfølgende fattigdom og kaos, førte til en kraftig økning av transnasjonal prostitusjon. I Oslo og andre norske byer ble det åpnet bordeller, som oftest i boligstrøk. Naboer og feminister – særlig fra den nystiftede Kvinnegruppa Ottar – protesterte, og aksjonerte utenfor bordellene med krav om at de skulle stenges. Aksjonistene opplevde å bli trakassert av horekunder og å bli drapstruet av bordelleiere og mafia. Aksjonene ble rett og slett for farlige, og prostitusjonen fortsatte. En begrensende faktor var likevel at hallikparagrafen ble utvidet til å gjelde utleie av leiligheter der prostitusjon foregikk.</p>



<figure class="wp-block-image"><img src="https://lh4.googleusercontent.com/df6WMJDA18HEj0ZR5tBz8c2hE11eNok1YGsXFrKEO7r6gEboLeiDQZtVfk5q4kuMzLEbhdTMdPD8e9RIWgWH7BWTrXoGnfhA0BU-JjXq3oiyl224JdzKHE8l8q44ql--X530tTO8BqKaNhuaVA" alt=""/></figure>



<p><strong>Slaget om Finnmark</strong></p>



<p>Det var i Finnmark at kampen mot prostitusjon nådde et vendepunkt på slutten av 1990-tallet. Situasjonen der var at russiske jenter og kvinner ble fraktet med buss til campingplassen i den samiske bygda Skippagurra i Tana kommune, en bygd med 3000 innbyggere. Der ble desolgt til lokale så vel som tilreisende menn. Nestleder i SV, Kirsti Bergstø, var ung SUer i Øst-Finnmark på 1990-tallet. Hun fortalte på Ottars sexkjøpslovskonferanse om hvordan prostitusjonen preget hele det lille bygdesamfunnet og ble en del av mannskulturen. Skolegutter debuterte seksuelt med prostituerte, naboer og slektninger utnyttet purunge, russiske jenter. Prostitusjonen påvirket også hvordan de lokale guttene begynte å behandle jenter generelt; seksuell trakassering ble et stort problem. Russiske kvinner og barna deres ble kalt horer og mobbet.</p>



<figure class="wp-block-image"><img src="https://lh5.googleusercontent.com/NT_FapDa4Cqs9Pc7IPqKUQnFUbSWjKvZfwWZ7hb6Snx1n9UCnlvZce5ZlbgmZLBkZADozV_6MuN9Th66W4p5jeSFM-k0_taI12Ab3CcWj5DFQ1QPCKaM7o8PKTZXsIaFy_kr96Yi9EWTbY3zDA" alt="demo_tanakonferanse kopi"/></figure>



<p>Bygdefolk som ikke likte utviklingen følte seg maktesløse, men begynte snart å organisere seg, i samarbeid med blant annet Kvinnegruppa Ottar og samiske kvinneorganisasjoner fra Finland, Norge og Russland . Motstanderne var farlige. En av dem som organiserte prostitusjonen, hallikdømte Frank Sandberg, kom med trusler. Demonstranter ble forsøkt kjørt ned. Da campingplassen etter flere års kamp ble stengt i 2000, var det smittevernloven som ble brukt for å stenge den, med kommunelege Sigrun Windterfeldt i front.&nbsp;&nbsp;</p>



<p><strong>Sverige vedtar sexkjøpsloven&nbsp;</strong></p>



<p>Kravet om å kriminalisere horekunder fikk fornyet kraft på slutten av 1990-tallet. En viktig grunn til dette var at Sverige i 1998 vedtok å forby sexkjøp som ledd i en lovpakke for å bekjempe menns vold mot kvinner, den såkalte <em>Kvinnofridslagen </em>(<em>Kvinnefredsloven</em>). Som i Norge, var det den svenske venstresida som sikret flertall for sexkjøpsloven, mens høyresida var imot.&nbsp;</p>



<p>I Oslo vedtok 8. marskomiteen i år 2000 «Kriminaliser horekundene!» som en av hovedparolene. Venstresidas ungdomspartier og RV (forløper for Rødt) gikk inn for sexkjøpsloven, og snart kom LO med på laget.&nbsp;&nbsp;</p>



<figure class="wp-block-image"><img src="https://lh4.googleusercontent.com/u_F2Dbvbcf5aRZU9p-3a1t3rmTPEP9yTDrj9jZfqRiw1DhGck6ox1ap5A8bVPGBMKM1kRN0r2z7Q9CyhNNuDa6-TEcBphiwz2uDwxVir-J68uqPeMzWltQLLRq93qwtxKb6BxuoFMPfv2xE6Xg" alt=""/></figure>



<p><strong>LO og sexkjøpsloven</strong></p>



<p>I 2001 ble Gerd Liv Valla valgt til tidenes første kvinnelige LO-leder. I denne mektige posisjonen satte hun kvinnesaker på dagsordenen i langt sterkere grad enn sine mannlige forgjengere. Foruten radikale kvinnekrav som 6-timers normalarbeidsdag, kjønnskvotering og likelønn, gikk Valla inn for å kriminalisere horekunder.&nbsp;</p>



<p>På Ottars sexkjøpslovskonferanse fortalte Valla om prosessen som førte til at LO vedtok å gå inn for sexkjøpsloven. Det første steget var at LO-kongressen i 2005 vedtok forbud mot at tillitsvalgte kunne kjøpe sex og en vurdering av sexkjøpsforbud som ett av flere tiltak mot trafficking. I sin 1.mai-tale i 2006 tok Valla til orde for kriminalisering av sexkjøp. Og gjennom LO-lederens plass i Arbeiderpartiets sentralstyre påvirket hun partiet. AUF og Oslo Arbeiderparti hadde allerede tidligere gått inn for sexkjøpsloven. Men det fantes motstand i den internasjonale fagbevegelsen.&nbsp;&nbsp;</p>



<p>På en kongress i den europeiske fagbevegelsen i 2006 kom det opp forslag om å anerkjenne prostituerte som arbeidstakere. Men danske, norske og svenske LO gikk imot. De holdt fast ved fagbevegelsens tradisjoner om å ville bekjempe prostitusjon, ikke legge til rette for det. Det var en parallell til diskusjonene om barnearbeid. Mens noen har argumentert for at en &laquo;ikke skal ta brødet fra barna&raquo;, har fagbevegelsen besluttet å gå imot barnearbeid og heller finne andre løsninger på fattigdomsproblemer. LO-rådgiver Jonas Bals påpekte også på Ottars konferanse at prostitusjonsforsvareres sammenlikning mellom prostitusjon og hardt kroppsarbeid er dobbelt urettferdig; den bagatelliserer prostitusjonens vold og overgrep, samtidig som den overdriver hvor ille det er å jobbe i fysisk krevende arbeiderklasseyrker. <br>LO endte altså med å støtte kriminalisering av sexkjøp, og det samme gjorde partiene i den rød-grønne regjeringen slik at det ble flertall i Stortinget for å vedta sexkjøpsloven 20. november 2008, med virkning fra 1. januar 2009. Men vedtaket skjedde ikke uten motstand.</p>



<p><strong>Motstanderne av sexkjøpsloven&nbsp;&nbsp;</strong></p>



<p>Målgruppa for sexkjøpsloven, sexkjøperne, er sjelden de som offentlig har frontet motstand mot loven, selv om de er den desidert mest tallrike gruppa i prostitusjonsfeltet. Motstanderne som uttaler seg offentlig har vært en sammensatt gruppe bestående av halliker, FrP-politikere, sosialarbeidere, forskere og de seneste årene menneskerettighetsorganisasjoner som FRI – Foreningen for seksualitetsmangfold og Amnesty.&nbsp;</p>



<p>På 1990-tallet var de fremste forsvarerne av prostitusjon halliker og kriminelle som truet prostitusjonsmotstandere med vold, samt FrP-politikere som Carl I. Hagen, som gikk inn for offentlige bordeller. Paradoksalt nok ble også hjelpeapparatet for prostituerte uttalte motstandere mot å bekjempe prostitusjon. Det samme ble enkelte forskere. Seinere har også PION, en &laquo;interesseorganisasjon» for «sexarbeidere&raquo; (PION er ikke en medlemsorganisasjon) gjort alt de kan for å normalisere prostitusjon, med inspirasjon fra og nettverk knyttet til det europeiske prostitusjonsmarkedet.&nbsp;</p>



<p>Da spørsmålet om kriminalisering av sexkjøp ble aktualisert på begynnelsen av 2000-tallet ble venstresidas partier og LO utsatt for en intens lobbyvirksomhet fra det nasjonale kompetansesenteret ProSentret og PION.. Enkeltstående prostituerte ble frontet overfor politikere, for eksempel på LO-lederens kontor, og på Arbeiderpartiets og SVs landsmøter, for å hindre dem i å vedta å gå inn for sexkjøpsloven. Lobbyvirksomheten og personfokuset bar frukter, og alle toppene i Arbeiderpartiet og SV, som fra 2005 satt i regjering, gikk derfor imot å innføre en sexkjøpslov. En mektig motstander mot sexkjøpsloven var justisminister Knut Storberget. Da APs landsmøte i 2007 gikk inn for å kriminalisere sexkjøp var det med 184 av 300 stemmer. Det var et grasrotopprør, med støtte av AUF, LO, og Arbeiderpartiet i Oslo og Hordaland.</p>



<p>Jan Bøhler fra Arbeiderpartiet ga på Ottars konferanse en levende beskrivelse av hvordan folk fra PION og ProSentret nesten fulgte etter delegatene opp på talerstolen for å hindre dem i å støtte sexkjøpsloven. Ifølge Bøhler fikk den intense lobbyvirksomheten til PION og ProSentret motsatt virkning på enkelte delegater.</p>



<p>Også i SV var ledelsen imot sexkjøpsloven, og også her kom PION og ProSentret på deres landsmøte for å forhindre dem i å gå inn for loven. Også i SV var det et grasrotopprør i partiet anført av SU og Hordaland SV, som gjorde at landsmøtet i 2007 vedtok å innføre sexkjøpsloven, etter å først å ha nedstemt dette i 2005.&nbsp;</p>



<p>Ifølge Kirsti Bergstø var grunnen til at det tok så lang tid før det feministiske partiet SV vedtok å støtte sexkjøpsloven at det i partiet er en holdning om at man &laquo;snakker med dem det gjelder&raquo;. I dette tilfellet var det PION og ProSentret som &laquo;representerte&raquo; de prostituerte, sistnevnte var til og med et &laquo;kompetansesenter&raquo;. Argumentet om at &laquo;man skal ikke gjøre livet vanskeligere for de prostituerte&raquo; falt i god jord hos en SV-ledelse som var preget av et sosialarbeiderperspektiv uten noen kjønnsmaktanalyse. Denne holdningen er langt fra unik for SV, og deles av mange velmenende personer.&nbsp;</p>



<p>Rødt (den gang RV) var en pådriver for sexkjøpsloven, men var ikke representert på Stortinget. Senterpartiet og Kristelig Folkeparti støttet innføring av loven, dog uten å dele venstresidas feministiske begrunnelse for den.&nbsp;</p>



<p><strong>Hva kan vi lære av utlandet?</strong></p>



<p>Det finnes i dag ingen land med en prostitusjonslovgivning som fungerer optimalt, ettersom patriarkalske kulturer i varierende grad overalt strukturerer lovgivning og håndhevingen av den. La oss likevel se på prostitusjonslovgivningen i to europeiske land som har valgt to helt ulike måter å løse prostitusjonsproblematikken på, nemlig Tyskland og Frankrike.&nbsp;&nbsp;</p>



<p>I 2002 liberaliserte Tyskland sin prostitusjonslovgivning med bred politisk støtte. Alle sider av prostitusjon ble legalisert. Argumentene var de samme som Amnesty og FRI fronter i dag, nemlig at det ville føre til tryggere «arbeidsforhold» for prostituerte, de kunne få pensjonsrettigheter, sykepenger og andre sosiale ytelser.&nbsp; Men bare 44 prostituerte ba om å bli registrert som dette i hele Tyskland! Tyske myndigheter har ingen systematisk oversikt over hvor mange kvinner som befinner seg i prostitusjon, estimat anslår alt fra 200&nbsp;000 til over en million.  Omlag 90 prosent er migrantkvinner, de fleste fra Øst-Europa, og en stigende andel flyktninger. Den manglende oversikten blir sterkt kritisert fra tysk kvinnebevegelse.</p>



<p>Legaliseringen førte til at den internasjonale sexindustrien fikk fritt spillerom, og Tyskland ble et økonomisk mekka for halliker. Snart oppsto bordeller med &laquo;flatrate&raquo; (&laquo;knull så mange du vil for 50 euro&raquo;), &laquo;gang bang&raquo;-fester, inkludert med gravide kvinner. Prisene raste og Tyskland ble en destinasjon for sexturisme. I løpet av de siste 25 årene er minst 84 kvinner blitt myrdet i den tyske prostitusjonshandelen, og 47 andre prostituerte har blitt utsatt for drapsforsøk. Lidelsene, skadene og traumene for de hundretalls tusener av kvinner som er blitt utnyttet i prostitusjon i disse årene, kan knapt tallfestes.&nbsp;</p>



<p>Den tyske prostitusjonspoltikken har vært så katastrofal at stadig flere ser at den ikke kan fortsette. I 2017 ble det vedtatt en reform som angivelig skulle beskytte de prostituerte, men som snarere har ført til en forverring fordi den tvinger prostituerte til å registrere seg, ta obligatoriske helsesjekker (for å sikre kundene en &laquo;smittefri vare&raquo;) og betale skatt.&nbsp;</p>



<p>Fra å ha blitt utskjelt og latterliggjort som puritanere, opplever tyske feministiske prostitusjonsmotstandere økende støtte. Ifølge Manu Schon fra de to venstresideorganisasjonene mot sexkjøp, <em>Stop Sexkauf</em> og <em>LINKE für eine Welt ohne Prostitution</em>, er det nå blitt trendy å være imot prostitusjon i Tyskland. Schon deltok på Ottars konferanse, og fortalte hvordan radikale feministers analyser deles av stadig flere, også av venstresidepolitikere, studentorganisasjoner og folk som tidligere definerte seg som liberalfeminister og &laquo;sexarbeidere&raquo;. I begynnelsen av juni 2019 bestemte kvinneutvalget til det sosialdemokratiske partiet SPD seg for å gå inn for en sexkjøpslov etter svensk-norsk mønster.&nbsp;</p>



