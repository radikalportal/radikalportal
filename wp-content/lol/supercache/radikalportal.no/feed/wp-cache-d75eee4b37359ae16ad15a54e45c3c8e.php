<p>&#8212; Det blir helt sikkert enklere for politiet å gjøre jobben sin om de bare kan forvise folk som de vil, men det er jo ikke den typen samfunn vi ønsker å leve i. </p>



<p>Saken skal opp for retten 29. oktober og sjøl om Stig Berntsen tror han vil vinne er han klar til å ta kampen enda lengre. </p>



<p>&#8212; Hvis jussen finner dekning for denne formen for inngrep i bevegelsesfriheten må loven endres. </p>



<p>Rødt Oslo fremmer om kort tid et forslag om at rasistiske organisasjoner ikke skal gis lov til å demonstrere i Oslo kommune. </p>



<p>Rødt Oslo har kommet med <a href="https://rødt.no/oslo/oslopolitiet-bruker-rene-politistat-metoder-mot-rodts-bystyrekandidat">en uttalelse</a> hvor de kaller metodene brukt av oslopolitiet for politistatsmetoder. De lover å støtte Berntsen i saken. Mange andre fra Rødt og den antirasistiske bevegelsen har kommet med støtteerklæringer til Berntsen.</p>



<p>Politiet i Oslo har ikke besvart Radikal Portals henvendelser. </p>
