<p><strong>Rødts prosjekt må være å utvide den demokratiske deltakelsen for å gå fra et tilskuerdemokrati til et deltakerdemokrati, med langt større grad av deltakelse fra vanlige folk i politiske prosesser.</strong></p>



<span id="more-35921"></span>



<p> For oss som vil skape en folkebevegelse som kan utfordre makta til kapitalen har flere utfordringer. Jeg vil trekke fram to.   </p>



<p>For det første, så må den delen av samfunnet som demokratiet rår over utvides. I dag ser vi dessverre at utviklinga går i motsatt retning. Avgjørelser flyttes fra folkevalgte organer til lukkede styrerom, eller til domstoler i EU eller WTO. </p>



<p>Min egen arbeidsgiver, Nord universitet, er et godt eksempel på dette. Når politikerne ønsker å legge ned studiesteder på Nesna og i Sandnessjøen, så er det ingen som går til valg på det. I stedet går man til valg på reform i høyere utdanning, så kutter man budsjetter og overfører beslutningsmakt til styret i Nord universitet, som til slutt tar en upopulære avgjørelsen om å legge ned studiesteder. Det samme ser vi med lokalsykehus over hele landet, også i min hjemby Mosjøen, hvor lokalsykehuset bygges ned bit for bit. Dette er dypt politiske spørsmål, som angår folks hverdag. Det er spørsmål for politikken, ikke for vedtak i anonyme styrer som ikke er på valg. </p>



<p>Så det er det første poenget, rommet som demokratiet rår over må utvides. Det er en sentral oppgave for et parti som Rødt. Det som skiller Rødt fra andre partier er at Rødt ikke vil stoppe med sykehus eller universiteter, men også demokratisere økonomien.  </p>



<p>Skal det være mulig å avskaffe kapitalismen, eller å bygge en motmakt til den rikeste prosenten i verden, så må det store flertallet av vanlige folk bli enige om et felles program for å gjøre det. Forutsetninga for at det skal skje, er at folk tror at det er mulig. </p>



<p>Da jeg var 20 år og studerte statsvitenskap, så var en av de store ideene at historien var slutt. Etter den kalde krigen hadde de store tingene som kom til å skje skjedd, nå skulle markedsøkonomi og det liberale demokratiet løse de gjenværende problemene. Dette var rundt år 2000. Kapitalismen fremstod som en naturtilstand. Slik er det ikke lenger. Groteske forskjeller, arbeidsledighet og avmakt gjør at folk reagerer mer og mer på denne urettferdigheten i dag. Det ser vi med Bernie Sanders i USA, vi ser det med Corbyn i Storbritannia, også Rødt sine målinger på 6-7 prosent er et uttrykk for dette her i Norge. </p>



<p>Så hva gjør vi nå? Hvordan bygger vi videre på dette, og sørger for at denne bevegelsen blir større, og ikke et blaff? Det er det store spørsmålet. Ikke bare fordi jeg ønsker meg et mer rettferdig samfunn, men fordi alternativet til at folk sitt sinne rettes dit det hører hjemme, er at det rettes mot syndebukker, enten det er trygdemottakere som må stå opp om morran, eller innvandrere som truer kulturen vår. Rødt sitt alternativ til høyrepopulismen, økende forskjeller og klimakrise er en demokratisering av samfunnet, at demokratiet gjøres dypere og bredere. Bredere fordi det må ha makt over en langt større del av samfunnet enn i dag, og dypere fordi vanlige folk må være en del av politikken i langt større grad enn i dag.  </p>



<p>Da må demokrati bli noe
mer enn å legge en lapp i en boks annet hvert år. Rødt må bidra
til at demokrati blir noe som alle føler seg berettiget til å delta
i. Det betyr at folk sine hverdagserfaringer må løftes fram. 
</p>



<p>Rødt må bli en megafon
for barnefamilien som ikke har råd til å reise på ferie fordi
pappa er uføretrygda.</p>



<p>Rødt må være tilstede
for de som mister jobben når hjørnesteinsbedriften blir nedlagt
fordi kapitalen kan tjene mer penger andre steder. 
</p>



<p>Og Rødt må backe opp de
som ikke får det de har krav på fra velferdsstaten, enten det er
NAV, pensjon eller et frikort som er brukt opp. 
</p>



<p>Kort sagt, Rødt må ta utgangspunkt i folk sine liv, folk sine hverdagserfaringer, når politikken utformes. Og politikken til Rødt må utformes av de som lever disse livene. For et parti som vil forandre samfunnet og utvide demokratiet, så holder det ikke å bli stemt inn på stortinget for å ordne opp for folk. Skal Rødt fylle sin funksjon, så må Rødt være et parti som kan gi folk nederst på stigen selvtilliten til å heve stemmen i offentligheten. For å skape troa på at vi kan forandre samfunnet, må vi ta utgangspunkt å folk sine hverdagserfaringer, og utvikle politikken derfra. For når vi forandrer verden, forandrer vi oss selv. Mennesket er skapende, kreative vesener. </p>



<p>Å gi folk troa på at denne energien kan brukes på egne vegne for å forandre verden, er Rødt sin oppgave i dag. At folk skal bli klar over sin egen handlekraft, i stedet for å dyrke sterke ledere, og føle at folk som Trump eller Sylvi Listhaug styrer på vegne av dem. Rødt skal ikke styre på vegne av noen som helst, Rødt må være et parti som gjør folk flest troa på at de kan heve stemmen, kreve å bli hørt og styre selv. </p>



<p>Derfor må Rødt sitt prosjekt være å utvide den demokratiske deltakelsen i samfunnet, slik at vi kan gå fra et tilskuerdemokrati til et deltakerdemokrati, med langt større grad av deltakelse fra vanlige folk i politiske prosesser. Og denne deltakelsen kan ikke være noe Rødt innfører etter vi har fått makt.  Å skape og bygge på deltakelsen i politikk fra folk flest må være selve fundamentet i enhver strategi for å få makt. Spesielt hvis målet er å forandre samfunnet, og ikke bare bytte ut noen politikere på Stortinget.  </p>
