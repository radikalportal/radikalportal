<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/08/16/politiet-slo-ring-rundt-rasmus-paludan/¨">Politiet slo ring rundt Rasmus Paludan</a></strong></p>



<p><strong>Mot Rasmus Paludan</strong><br>&#8212; Jeg og en venn hadde hørt at Rasmus Paludan skulle stå på Karl Johans gate for å spre hatet sitt. 150 meter før vi kom til valgboden til Selvstendighetspartiet blei vi stoppa av sivilt politi. Av den ene fikk jeg vite at jeg var «en kjent venstreekstremist». De ville vite hva vi skulle der og ba om legitimasjon. Jeg spurte om det var blitt politisk segregering av Oslo sentrum. Vi fikk gå videre, men blei fortalt at vi ville bli holdt under oppsikt, forteller <a href="https://radikalportal.no/author/stig-berntsen/">Stig Berntsen</a>, som er <a href="https://rødt.no/oslo/mot-kandidatene-stig-berntsen">niendekandidat</a> i kommunevalget for Rødt Oslo, til Radikal Portal. </p>



<p>Det var på torsdag at Selvstendighetspartiet (SHP) tok imot Rasmus Paludan, politikeren som tross enorm medieoppmerksomhet feilet i sommerens folketingsvalg i Danmark og tidligere har blitt dømt for rasistisk hets. Paludan er blitt berømt og beryktet for å brenne koranen innpakket i bacon på offentlige tilstelninger. </p>



<p>&laquo;Våre gater og smug vil bli forvandlet til elver av blod. Og de fremmede fienders blod vil ende i kloakken, der de fremmede fiender hører hjemme&raquo;, sa han før han blei kastet ut av ytre høyre-partiet Nye Borgerlige og stiftet sitt eget parti, Stram Kurs. <a href="https://filternyheter.no/vare-gater-skal-bli-elver-av-blod-de-7-verste-uttalelsene-til-paludan/">Han har uttalt</a> at det beste ville vært om det ikke fantes en muslim igjen på jorda, han vil kaste alle muslimer ut av Danmark og frata muslimer rett til å bruke motoriserte kjøretøy fram til det blir en realitet. Han mener hvite bør styre Sør-Afrika fordi &laquo;de fleste negere i Sør-Afrika ikke er begavet nok til å se hvordan tingene rent faktisk er&raquo; og at inuittene på Grønland også er for dumme til å få selvstyre fra Danmark. Også i Oslo kom han med <a href="https://radikalportal.no/2019/08/16/politiet-slo-ring-rundt-rasmus-paludan/">rasistiske provokasjoner</a>.</p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2017/07/29/voldsdomte-nazister-marsjerte-i-kristiansand-politiet-fjernet-motdemonstrant/">Voldsdømte nazister marsjerte i Kristiansand &#8212; politiet fjernet motdemonstrant</a></strong></p>



<p><strong>Bortvist etter forespørsel fra politisk motstander</strong><br>&#8212; Vi kommer fram til boden deres, tar noen bilder, noe representantene fra Selvstendighetspartiet protesterte på. Vel, hvis du stiller til valg og har satt opp bod på Karl Johan vil jeg si det er sånt du må finne deg i. Den ene truet med å spraye tåregass på oss, noe jeg informerte politiet om. Han gikk deretter til politiet og krevde oss fjerna. Han argumenterte med at min tilstedeværelse var en provokasjon.</p>



<p>&#8212; Det var da politiet kom og bortviste meg fra Oslo sentrum i 24 timer.</p>



<p>Radikal Portal har snakket med flere som har blitt trua med å sprutes ned med en type pepperspray og fått framvist en video hvor politiet skal ha beslagslagt en slik sprayboks fra en av SHPs aktivister.  </p>



