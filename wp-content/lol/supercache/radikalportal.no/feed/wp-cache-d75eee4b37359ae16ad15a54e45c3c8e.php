<p><strong>Det nyeste høyreekstreme terrorangrepet i USA møtes med
vegring i norsk presse mot å kalle det terror. </strong></p>



<span id="more-35912"></span>



<p>Lørdag kveld norsk tid ble
20 mennesker drept og 26 andre såret i et høyreekstremt terrorangrep på den
Latin-amerikanske befolkningen i Texas. Patrick Crusius (21) gikk inn i en Wal-Mart
i El Paso, Texas og skjøt kunder som skulle handle inn til det kommende skoleåret.</p>



<p>Så langt har norsk media
vegret seg mot å kalle angrepet det det er – terror. I de første meldingene fra
NRK kan vi lese at det har vært en «skyting», senere på kvelden omtaler media
det som en «massakre». Hverken ordet «terror» eller «angrep» blir brukt av
norsk media for å beskrive hva som har skjedd.</p>



<p>Vi kan sammenligne dette med
terrorangrepene mot Bataclan i 2015 og Christchurch tidligere i år, hvor media
var raskt på ballen og kalte hendelsen nøyaktig det det var – terror. Hvorfor
vegrer de seg nå?</p>



