<p>Om vi igjen ser tilbake på
Christchurch og Bataclan var media raske med å kalle det som hadde skjedd et
angrep. Er det sånn å forstå at <em>noe annet</em>
skjedde i Christchurch og Bataclan enn det som skjedde i El Paso, siden det var
angrep og <em>dette ikke er det</em>?</p>



<p>Økningen av høyreekstrem
vold og terror i USA er et alvorlig fenomen. Ordene norsk media bruker på å beskrive
dette fenomenet kommer imidlertid til kort. Å snakke om «skytinger» og «massakrer»
er ikke dekkende og bidrar ikke til noen dypere forståelse av hva som faktisk
foregår. Et land som opplever en rekke sivile massakre og et land som opplever en
rekke terrorangrep er begge land i forfall. Karakteren av forfallet er allikevel
forskjellig.<br>
<br>
Det kan eksistere tusen skolemassakre i USA uten noe politisk mål og disse er
vesensforskjellige fra massakre med den hensikt å spre frykt for å nå et
politisk mål – terrorisme. Det som skjedde i El Paso er et klart eksempel på
terrorisme og det er akkurat dette norsk presse burde omtale det som. &nbsp;</p>
