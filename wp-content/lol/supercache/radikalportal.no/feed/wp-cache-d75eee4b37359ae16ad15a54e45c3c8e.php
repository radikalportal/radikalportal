<p>&#8212; Jeg forteller politiet at dette finner jeg meg ikke i. Politiet hjelper her et parti fra ytre høyre med å bortvise meningsmotstandere fra Oslo Sentrum. De blir et politisk politi og forhindrer valgkamparbeidet til Rødt. </p>



<p>Berntsen blei så ført bort av en politipatrulje og satt på glattcelle i fire timer. Med seg ut derfra fikk han en bot på 9500 kroner for å ha nekta å etterkomme politiets ordre. </p>



