<p>Vi har med andre ord et klart politisk mål og en taktikk som baserer på spredning av frykt for å nå dette målet. Angrepet i El Paso er et skoleeksempel på terrorisme, allikevel vil ikke norsk media kalle det dette. De ville ikke engang <em>anta</em> at det er terrorisme selv om <em>alle</em> piler peker den veien, som du har sett i denne teksten </p>



<p><strong>Angrep eller skyting</strong><br>Samtidig kan man si at å anta at angrep på sivile i USA umiddelbart er terrorisme ofte kan få nyhetssider til å rapportere forhastet og feilaktig. Landet har allerede sett <a href="https://www.gunviolencearchive.org/reports/mass-shooting">utallige</a> angrep på sivile så langt i år, selv om ikke alle er selverklært terrorisme. Hvis vi et sekund glemmer at alle pilene i dette tilfellet peker mot terrorisme, og at dette er fullstendig underrapportert av norsk media, er det allikevel rart at de <em>ikke engang</em> beskriver det som har skjedd som et <em>angrep</em>. <br> <br> Uavhengig av om det eksisterer et politisk motiv, er enhver masseskyting et angrep. Å kalle noe et angrep forutsetter en handlende aktør, et offer og et mål. <em>Alle</em> masseskytinger har dette, uavhengig av om målet er uttalt politisk eller ei. Ordene massakre og skyting på sin side er mer passive. Massakre har bare en gjerningsmann og offer, imens en skyting bare har en gjerningsmann.</p>



<p>Dette kan virke som
flisespikkeri, men det er merkelig at media, når de skal dekke alvorlige
nyhetssaker, ikke bruker språk som klart beskriver hva som har skjedd. <em>Alle</em> <em>masseskytinger
er angrep</em>. De er ikke bare massakre eller «skytinger». </p>



