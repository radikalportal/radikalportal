<p><strong>Å fokusere ensidig på overbefolkning i en verden hvor nesten alt forbruk foregår hos en liten minoritet, er fullstendig skivebom. De økologiske krisene i verden henger først og fremst sammen med at vi har et økonomisk system som forutsetter evig vekst og grådighet – ikke at fattige mennesker føder flere barn enn rike.</strong></p>



<span id="more-35932"></span>



<p>«Vi er for mange på
jorda! Det er umulig å gjøre noe med miljøødeleggelsene dersom
folk skal fortsette å få så mange barn!»</p>



<p>Vi har alle sett slike
responser på artikler som omhandler klimaendringer eller
miljøødeleggelser. Argumentet går gjerne slik: vi blir flere og
flere mennesker i verden, og veksten skjer primært i fattigere deler
av verden. Følgelig er det fattigfolk i Afrika og Asia som må ta
ansvar og få færre barn. Underforstått: vi som får få barn kan
slappe av og fortsette som før.</p>



<p>Det kan virke smått
komisk at folk i Norge, som trolig er mer opptatte av å unngå fedme
og overvekt enn sult, skal belære folk i fattige land om
konsekvensene av å leve på en planet med begrensa ressurser. For
det første er utgangspunktet gjerne grunnleggende rasistisk, all den
tid det baserer seg på forestillinga om at det bare er folk i
fattige land som bidrar til overbefolkninga, all den tid også
befolkningsveksten i den såkalte «tredje verden» har stupt minst
like fort som i rike land de siste tiårene. For det andre
underkjenner det komplett at de fleste mennesker i verden knapt har
noe forbruk å snakke om, mens de rikeste på planeten lever i
grenseløs overflod.</p>



<p>Likevel er det en
innstilling vi må ta seriøst. Det er ingen tvil om at
befolkningsveksten representerer store utfordringer. Om
utgangspunktet vårt er at alle mennesker i verden har rett på gode
liv uten fattigdom og nød, krever dette enorme omlegginger. Dette
behovet vil også uunngåelig kunne komme i konflikt med hensynet til
miljø og natur. FNs siste rapport om biologisk mangfold var
graverende lesning. Menneskelig aktivitet utrydder allerede
økosystemer i et forrykende tempo, og verre vil det bli om vi ikke
snart endrer kurs.<a href="#sdfootnote1sym"><sup>1</sup></a></p>



<p><strong>Arealbruk og
matproduksjon</strong></p>



<p>Vi lever i en paradoksenes
tid. Allerede produserer vi mat nok til 10 milliarder mennesker.<a href="#sdfootnote2sym"><sup>2</sup></a>
Samtidig er 821 millioner mennesker underernærte.<a href="#sdfootnote3sym"><sup>3</sup></a>
De har ikke mat nok til å overleve. For nordmenn forholder det seg
motsatt. I fjor var om lag 70 % av den voksne befolkninga
overvektige.<a href="#sdfootnote4sym"><sup>4</sup></a>
Hos barna har andelen stabilisert seg på om lag 20 %.<a href="#sdfootnote5sym"><sup>5</sup></a>
Samtidig kaster vi om lag 357&nbsp;000 tonn mat her til lands,
årlig.<a href="#sdfootnote6sym"><sup>6</sup></a>
Det er ingen grunn til å tro at Norge er blant de verste i vesten på
dette. Når det kommer til mat står vi altså overfor et ekstremt
sløseri- og fordelingsproblem i verden – ikke et knapphetsproblem.</p>



<p>Mye av dette handler om
arealbruken. FNs klimapanel kom nylig med en rapport som tydelig
problematiserer hvordan vi produserer mat i verden.<a href="#sdfootnote7sym"><sup>7</sup></a>
Moderne matproduksjon baserer seg på mye bruk av fossilt drivstoff,
store arealer avsatt til å produsere fôr til husdyr. Dette legger
beslag på enorme arealer som kunne ha blitt brukt til å produsere
menneskemat. Det er for eksempel estimert at mellom 61 og 75 % av
regnskogødeleggelsen i Brasil skyldes landets produksjon av
storfekjøtt for eksport.<a href="#sdfootnote8sym"><sup>8</sup></a></p>



<p>Norske matvaner spiller
også en rolle her. Norsk oppdrettsnæring legger gjennom sitt
forbruk av soya beslag på 1&nbsp;506 kvadratkilometer med arealer i
Brasil. Den norske kjøttindustrien legger på sin side beslag på
377 kvadratkilometer.<a href="#sdfootnote9sym"><sup>9</sup></a>
Dette tilsvarer 9&nbsp;414 brasilianske småbruk. 
</p>



<p>Mesteparten av dette kunne
selvsagt heller blitt brukt til å produsere menneskemat direkte. Så
lenge det er etterspørsel i Norge og andre rike land etter
soyabasert kraftfôr for å holde i gang egen industriell
kjøttproduksjon, vil naturen og sjølforsyninga i andre land bli
skadelidende. 
</p>



<p><strong>Befolkningsveksten
stagnerer – hva mer kan vi forvente?</strong></p>



<p>Selv med dagens enorme
overforbruk av mat i vestlige land, produserer vi som nevnt fortsatt
mat til 10 milliarder mennesker. Sjøl uten en strukturell omlegging
av matproduksjonen slik FNs klimapanel anbefaler, vil det altså i
teorien være helt mulig å utrydde sult gjennom en endring av
fordelinga.</p>



<p>Likevel veit vi at vi vil
bli flere mennesker. Estimatene varierer, men de fleste prognosene
lander et sted mellom 10 og 14 milliarder mennesker en gang i
framtida.<a href="#sdfootnote10sym"><sup>10</sup></a>
Befolkningsveksten har dermed allerede stagnert. Økninga kommer ikke
av folk får mange barn. Tvert imot vil antall barn i 2100 være ca.
det samme som i dag, om lag to milliarder. Den generelle
befolkningsøkninga kommer fordi flere folk lever lengre, og fordi de
enorme generasjonene som ble født for 20-30 år sia skal ha ca. 2
barn per kvinne.</p>



<p>Her
vil enkelte innvende: «dette er umulig, vi må bli enda færre!».
Spørsmålet mitt blir da: hvordan? Hva skal man gjøre for å bremse
befolkningsveksten utover dette? Det globale gjennomsnittet for
antall barn per kvinne ligger på ca. 2,4 barn. I mesteparten av den
rike delen av verden er tallet under 2, og indikerer derfor en
negativ befolkningsvekst.</p>



<p>Så hvordan skal en
ytterligere begrensning foregå? Skal bevisst sulte i hjel mennesker
gjennom kutt i nødhjelp og bistand og stenging og militarisering av
grensene? Flere kriger? Hvor mange hundre millioner vil man drepe før
det er «nok»?</p>



<p>De fleste er sjølsagt
ikke så gærne at de åpent forfekter slike standpunkt. Derimot ser
jeg flere ta til orde for en global ettbarnspolitikk. Her kan man
spørre hvordan dette skal foregå i praksis. Å oppnå enighet om
dette globalt i dag, med de enorme forskjellene i hvor mye hvert
menneske legger beslag på, er rett og slett rein drømmetenkning.
Dessuten vil det trolig ikke ha mye å si for det globale forbruket
engang. Befolkningsveksten vil uansett bli formidabel med tanke på
hvor mange mennesker i fruktbar alder som foreløpig lever uten barn.
Fortsetter vi med dagens kurs, uten å endre måten vi lever på, men
samtidig på magisk vis klarer å overtale alle verdens 193 land til
å innføre ettbarnspolitikk, vil vi fortsatt fortsette mot kollaps
akkurat som i dag.</p>



<p>Konklusjon: mange land vil trolig ha godt av å begrense egen befolkningsvekst, men dette er langt ifra tilstrekkelig om vi ønsker at alle mennesker skal leve gode liv, uten at vi ødelegger planeten vår. Stadig mas om at fattige mennesker får for mange barn, er som oftest bare en hvilepute for rike folk i vesten som lukker øynene for hvordan vår egen livsstil ødelegger planeten vår.</p>



<figure class="wp-block-image"><img src="https://radikalportal.no/wp-content/uploads/2019/08/68969594_654733055026926_4288210139917320192_n.jpg" alt="" class="wp-image-35934" srcset="https://radikalportal.no/wp-content/uploads/2019/08/68969594_654733055026926_4288210139917320192_n.jpg 950w, https://radikalportal.no/wp-content/uploads/2019/08/68969594_654733055026926_4288210139917320192_n-150x109.jpg 150w, https://radikalportal.no/wp-content/uploads/2019/08/68969594_654733055026926_4288210139917320192_n-300x218.jpg 300w, https://radikalportal.no/wp-content/uploads/2019/08/68969594_654733055026926_4288210139917320192_n-768x559.jpg 768w" sizes="(max-width: 950px) 100vw, 950px" /><figcaption>Illustrasjon og statistikk fra <a href="https://www.oxfam.org/en/research/extreme-carbon-inequality">Oxfam</a>.</figcaption></figure>



<p><strong>Økologisk fotavtrykk –
mer enn bare mat</strong></p>



<p>Man ser for eksempel lett
at de fleste rike land jamt over har høyere økologisk fotavtrykk
per person enn fattige land. Norge har for eksempel et betydelig
høyere økologisk fotavtrykk per person enn både Kina, og ikke
minst India.<a href="#sdfootnote11sym"><sup>11</sup></a></p>



<p>For Norges del, siden vi
har latt den skitneste industrien flagge ut til fattigere land, er
det største problemet vårt private forbruk. 
</p>



<p>En gjennomsnittlig
europeer forbruker fire ganger så mye ressurser som en
gjennomsnittlig afrikaner, og tre ganger så mye som en
gjennomsnittlig person i Asia. Norge stiller i en særklasse med et
forbruk 37 % høyere enn gjennomsnittet i Europa.<a href="#sdfootnote12sym"><sup>12</sup></a>
Skulle resten av verdens allerede 7 milliarder innbyggere levd som
oss i Norge, ville vi trengt tre jordkloder for at ressursgrunnlaget
ikke skulle ha gått i minus.</p>



<p>Sagt annerledes: en
gjennomsnittlig nordmann veier opp for svært mange gjennomsnittlige
folk i den fattige del av verden. Fattigfolk i Afrika skal altså få
temmelig mange barn før de veier opp overforbruket til en rik
nordmann.</p>



<p>Et godt eksempel som angår
noe vi alle bruker, er klær. I 2016 eide hver nordmann i
gjennomsnitt 80 kg. klær hver, tilsvarende 359 plagg.<a href="#sdfootnote13sym"><sup>13</sup></a>
Hver av oss kaster ca. 23 kilo av klærne våre årlig.</p>



<p>Dette har mer betydning
enn vi kanskje tror. En rapport fra FN viste for eksempel at den
globale klesindustrien står for større utslipp enn både fly- og
skipstrafikken.<a href="#sdfootnote14sym"><sup>14</sup></a>
I tillegg står moteindustrien i dag for 20 % av alt avfallsvann –
noe som bør være en tankevekker, all den tid vi veit at om lag
halvparten av menneskeheten opplever årlige perioder med
vannmangel.<a href="#sdfootnote15sym"><sup>15</sup></a></p>



<p>Eksemplene er uendelige.
Alt vi kjøper av materielle ting, eller til og med tjenester, har et
økologisk avtrykk. 
</p>



<p>Poenget er altså klart:
gjennomsnittsnordmannen har et skyhøyt personlig forbruk og legger
dermed beslag på store deler av jordas areal og ressurser
sammenligna med folk i andre land. Det er komplett umulig at resten
av menneskeheten får leve som oss. Spørsmålet er dermed: skal vi
slåss for å opprettholde denne skeivheten, eller skal vi prøve å
møtes på halvveien?</p>



<p><strong>Industrialisering i
fattige land – en forutsetning</strong></p>



<p>Mesteparten av
befolkningsøkninga er forventa å komme i Afrika sør for Sahara.
Dette er en region som i stor grad fortsatt er prega av svak
økonomisk utvikling, mangel på industrialisering, og med et svært
lite intensivt landbruk. Skal disse landene være i stand til å bære
denne uunngåelige befolkningsveksten, vil de bli nødt til å
industrialisere. Det er nemlig en kjent sak at industrialisering er
en forutsetning for å øke produksjonen i landbruket, hvilket blant
annet forklarer at land som Nederland og Belgia, som etter alle
solemerker er klart mer «overbefolka» enn noe afrikansk land,
klarer å brødfø befolkningene sine gjennom en kombinasjon av
sjølforsyning og import. Industrialisering er en forutsetning både
for å kunne håndtere store befolkninger, samtidig som at det er det
beste verktøyet for å få ned befolkningsveksten.</p>



<p>Spørsmålet blir da om
rike land er interesserte i at Afrika skal få industrialisere. De
siste tiårene har rike lands politikk gjennom både FN, WTO, IMF og
Verdensbanken heller gått ut på det motsatte: tving fattige land
til å åpne markedene sine for sterkt subsidierte vestlige produkter
som dermed slår beina under lokal produksjon, den såkalte
«Vanek-Reinert-effekten». Dette har blitt rettferdiggjort gjennom
argumenter om at de fattige landene kan bli rike bare de får
eksportere landbruksprodukter og mineraler gjennom såkalt «fri
handel». At ingen land i verdenshistoria har blitt rike gjennom en
slik strategi, har man oversett. 
</p>



<p>Konsekvensen var
forutsigbar: de fleste land i Afrika sør for Sahara, i takt med
avindustrialiseringa i den «nyliberale æraen» i tiårene fra 1980,
opplevde fallende reallønninger.<a href="#sdfootnote16sym"><sup>16</sup></a></p>



<p>Konklusjon: skal fattige
land klare å bære befolkninga si i framtida, er rike land nødt til
å la dem bygge opp egne industrier gjennom proteksjonistisk
tollpolitikk, slik alle andre land som har industrialisert har fått
gjøre. 
</p>



<p><strong>System og personlig
moral</strong></p>



<p>Sosialister vegrer seg
normalt imot å legge ansvar over på vanlige arbeidsfolk. Tross alt
er det ikke vanlige arbeidsfolk som sitter med enorme bilparker og
flyr privatfly flere ganger i uka. De 10 % rikeste i Norge slipper ut
hele tre ganger så mye CO2 som de fattigste 10 prosentene.</p>



<p>Dette er en viktig
innvending. Det er dessuten en kjent sak at 100 av verdens selskaper
står bak 71 % av verdens totale klimautslipp.<a href="#sdfootnote17sym"><sup>17</sup></a>
Et systemskifte, og ikke bare flikking på folks vaner og forbruk, er
altså nødvendig.</p>



<p>Likevel kamuflerer denne
statistikken noe vesentlig. Storselskapene har nemlig ikke bare
milliardærer til kunder. Også helt vanlige folk med «vanlig»
inntekter kan oppgi «shopping» som en naturlig fritidssyssel, og
inneha et kvantum av personlige eiendeler som det overveldende
flertall av jordas befolkning bare kan drømme om. 
</p>



<p>Dette er aldeles ikke
alltid et spørsmål om valg. Skal man fungere i dagens samfunn er
man stort sett nødt til både å ha egen datamaskin, mobiltelefon,
og også ofte egen bil.  Ofte er det også billigere å kjøpe nytt
enn å reparere, hvilket gjør det absurd ugunstig for folk å ta
vare på eiendelene sine framfor bare å kjøpe noe flunkende nytt om
det gamle går i stykker. 
</p>



<p>Dessuten er det opplagt at
en politisk strategi for å forskyve ansvar og skyld mot vanlige folk
både er usedvanlig råtten, men også destruktiv. Se for eksempel på
Frankrike, hvor hele staten knaker i sammenføyningene på grunn av
usosiale miljøavgifter som rammer vanlige folk. 
</p>



<p>Det bør være åpenbart
for alle som kaller seg sosialister at en miljøpolitikk som tar
sikte på å ramme overforbruket i vesten <em>må
</em>være retta først og fremst mot
storkapitalens herjinger. Regninga kan ikke sendes til folk flest. En
karbonavgift til fordeling (KAF) kan for eksempel være et godt
utgangspunkt. En slik ordning har i det minste potensial til å få
ned utslippene drastisk, og samtidig gjøre at de fleste av oss vil
kunne gå i pluss. Venstresidas løsninger må være tydelige
systemkrav – ikke individuelle oppfordringer.</p>



<p><strong>Det handler om å redde
vår jord</strong></p>



<p>Jeg håper at jeg gjennom
denne teksten har vist hvordan overbefolkning i seg sjøl er et
mindre problem enn overforbruk. Det økologiske fotavtrykket i rike
land er alt for høyt, og det økologiske fotavtrykket til mange i
fattige land er så lavt at de ikke er i stand til å leve
fullverdige liv som mennesker. Fattigdom og skeivfordeling er de
største problemene. Om den fattigste halvdelen av menneskeheten
forsvant over natta, ville fortsatt de globale utslippa og det
ekstreme overforbruket i den rike delen av verden være mer enn nok
til å skyve oss videre utafor stupet.</p>



<p>Det er viktig at de
politiske krava likevel ikke begrenses til å handle om personlig
moral og individuell livsstil. Kjerna i problemet ligger tross alt i
det kapitalistiske systemet som fordrer evig akkumulasjon og vekst –
ikke bærekraft og menneskelige behov. Uten en demokratisk kontroll
over økonomien, vil vi stå uten de nødvendige verktøyene for å
stanse ødeleggelsen av planeten vår.  Det var ikke uten grunn at en
rapport fra FN i fjor konkluderte med at «markedet tar livet av
planeten vår» og at vi trenger «et nytt økonomisk system».<a href="#sdfootnote18sym"><sup>18</sup></a></p>



<p>Dette systemet kan ikke
være noe annet enn et grønt sosialistisk folkestyre.</p>



