<p>Alle nettavisene henviser til eksistensen av et manifest – altså en <em>politisk begrunnelse</em> for handlingen hans – men begrenser seg til å si at det etterforskes hvorvidt det er ekte. At manifestet inneholder beskrivelser av hva slags våpen han vil bruke, som er klart synlig og dermed enkelt å verifisere ut fra overvåkningsbildene som har blitt sluppet, nevnes ikke.</p>



<p><strong>De rette incentivene</strong><br>Før terrorangrepet publiserer gjerningsmannen et manifest på nettstedet 8chan. Der gjør han klart rede for sine <em>politiske</em> motiver, hva slags utstyr han bruker, hvordan lignende aksjoner best kan utføres av andre og en oppfordring om at andre «patriotiske amerikanere» skal gjøre lignende aksjoner. </p>



<p>For å unngå spredning,
