<p><strong>200 år etter er Peterloo-massakren fremdeles et symbol for klassehatets panikk mot en arbeiderklasse som krevde endring og forbedring, men også en inspirasjon for kommende kamper.</strong></p>



<span id="more-35977"></span>



<p>16. august var det 200 år siden «Peterloo»-massakren i Manchester, England. Dagen da titusenvis av fredelige demonstranter samlet seg i St Peter’s Field for å kreve parlamentariske reformer. Dagen da demonstrantene ble angrepet av det lokale kavaleriet. Dagen da 18 til slutt ble drept og flere hundre ble skadet. Dagen som ble inspirasjon for Percy Bysshe Shelleys dikt «The Masque of Anarchy» og nå nylig en ny film («Peterloo») av Mike Leigh. <br></p>



<p>Hovedtaleren i St Peter’s Field den 16. august 1819 var Henry Hunt. Hunt og andre reformister ville blant annet ha bedre representasjon i parlamentet, utvidet stemmerett og en slutt på barnearbeid. Deres ideer og popularitet blant den stadig voksende arbeiderklassen ble sett på som en trussel, og myndighetene i Manchester fryktet opptøyer.&nbsp;<br></p>



<p>Da Hunt begynte å tale, ble byens konstabler bedt om å arrestere ham. Den lokale «yeomanry», et frivillig kavaleri bestående av fabrikkeiere, handelsmenn og butikkeiere, og «husaren» (hærens kavaleri) ble sendt inn for å beskytte konstablene og for å holde orden. Men i den trange folkefylte plassen hvor det stod 60.000-100.000 mennesker, ble det fullstendig kaos. Kavaleriet hadde lite plass å manøvrere seg i og yeomanry gikk amok med hest og sabel blant demonstrantene. I løpet av bare noen få minutter ble elleve drept og flere hundre skadet (ytterlige syv døde som følger av skadene sine).</p>



<p><strong>LES OGSÅ av </strong><a href="https://radikalportal.no/author/daniel-lees-fryer/"><strong>Daniel Lees Fryer</strong></a><strong>: </strong><a href="https://radikalportal.no/2016/10/04/slaget-ved-cable-street-en-seier-for-antifascismen/"><strong>Slaget ved Cable Street &#8212; en seier for antifascismen</strong></a></p>



<p><strong>Klassehat og kvinnehat</strong><br>Historikeren E. P. Thompson kaller Peterloo en sentral hendelse i Englands sosiale og politiske historie, og han beskriver angrepet som «the panic of class hatred» (klassehatets panikk). Det er vel lett å forstå hvorfor. Arbeidere i verdens raskeste voksende industriby ville ha forandring. Etter flere år med krig (Napoleonskrigene tok slutt i 1815), ødelagte avlinger («året uten sommer» var i 1816) og økte importavgifter («Corn Laws» ble først introdusert i 1815) gikk matprisene opp og gjennomsnittslønnen ned. Misnøyen tok ulike former: Det ble folkemøter, protester og opptøyer, sabotasje, streik og opprør. (Vel å merke var det fortsatt ulovlig å organisere seg i følge Combinations Act fra 1799, «an act to prevent unlawful combinations of workmen».) Manchester and Salford Yeomanry ble opprettet i 1817 for å stanse dette antatte oppvigleriet. Men yeomanry ble latterliggjort i aviser som Manchester Observer, og i St Peter’s Field gikk de til angrep mot folk med bannere og folk de kjente igjen fra tidligere demonstrasjoner og møter. De var bitre, de var nok redde—og noen var visst fulle—og hatet de bar med seg ble utløst i den spente situasjonen.&nbsp;<br></p>



<p>I tillegg til dette klasseperspektivet er det også verdt å merke at et uforholdsmessig stort antall drepte og skadede var kvinner. Stadige flere kvinner jobbet i Manchesters voksende tekstilindustri. Mange hadde møtt opp i St Peter’s Field og noen krevde stemmerett for alle. Den nystiftede Manchester Female Reform Society var for eksempel til stede og var godt synlig i sine hvite klær. Angrepet på de som var samlet i St Peter’s Field den dagen bygget antakeligvis ikke bare på klassehat, men også kvinnehat.</p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/06/07/arbeiderbevegelsens-rolle-i-unionsopplosningen/">Arbeiderbevegelsens rolle i unionsoppløsningen</a></strong></p>



<p><strong>Etter Peterloo</strong><br>Massakren ble rapportert i pamfletter og flere lokale og nasjonale aviser. (Det var Manchester Observer som først brukte navnet Peterloo—etter slaget ved Waterloo noen år tidligere—for å betegne det som skjedde.) Det var mye sympati og støtte for demonstrantene og sterke fordømmelser mot myndighetene. Men reaksjonen fra regjeringen ble innføring av nye, strengere lover. Disse lovene skulle blant annet forhindre store folkemøter, kneble radikale aviser og gjøre det lettere for å sikte og arrestere antatte bråkmakere.&nbsp;<br></p>



<p>De parlamentariske reformene Hunt og andre kjempet for ble det ikke noe av. Det måtte flere hendelser og bevegelser til for at det skulle skje. Men Peterloo er utvilsomt en viktig og sentral del av Englands sosiale og politiske historie og ikke minst arbeiderbevegelsens historie.&nbsp;<br></p>



<p><strong>Ye are many, they are few</strong><br>Da poeten Percy Bysshe Shelley fikk høre om massakren skrev han diktet «The Masque of Anarchy». Det ble ikke utgitt før etter hans død, flere år etter Peterloo, men diktet fanget opp det grusomme som skjedde i Manchester samtidig som det ble et kamprop mot urett og en sterk oppfordring til sivil ulydighet og ikke-voldelig motstand.&nbsp;<br></p>



<p>«Rise, like lions after slumber / In unvanquishable number! / Shake your chains to earth like dew / Which in sleep had fallen on you: / Ye are many—they are few!»</p>
