og Alliansen bør ikke få stand på Arendalsuka</a></strong></p>



<p><strong>Innrøm at du har driti deg ut</strong><br>Det er ironisk at Bjønnulv avslutter tilsvaret sitt med å si «Vi må nemlig velge mellom dialog og gevær», når det virker som han ettertrykkelig har valgt det siste. I stedet for å ta til seg kritikken eller gi så mye som en tomme til kritikerne, går han til fullstendig frontalangrep. Han påstår at Aftenposten ikke kan lese, at han aldri har fornektet holocaust og snur på flisa når det kommer til meningsinnholdet i lederen, samtidig som han lar referansen til Faurisson forbli pinlig ukommentert. </p>



<p>I samme
åndedrag anklager han Aftenposten for å mene at et moralsk forsvar av
ytringsfriheten er moralsk forkastelig. Hvordan kan dette forstås som en
invitasjon til debatt heller enn en krigserklæring? </p>



<p>Når det kommer til holocaustfornektelse er det tydelig at Bjønnulv enten har driti seg ut eller faktisk mener det. Selv om det virker mistenkelig å henvise til en verdensberømt holocaustfornekter uten selv å forstå hva man gjør, er det ikke umulig. Men da må han ta stilling og ikke prate det bort. Enten må han innrømme at han har driti seg ut eller avklare en gang for alle at han støtter innholdet i Faurissons bok. </p>



<p>Inntil det gjøres vil redaktørens holocaustfornektelse på lederplass stå som en skamplett i Folk er Folks historie. </p>
