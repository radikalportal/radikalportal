<p><strong>I ethvert samfunn der folk blir fratatt undervisning på sitt eget morsmål betyr det at loven i det landet gjelder kun for ett folk som hersker over andre folk.</strong> </p>



<span id="more-36079"></span>



<p>Zara Mohammadi er 29 år gammel, født i den østkurdiske landsbyen Sarvaleh nær byen Dehgolan, men bosatt i byen Sanandaj de siste årene.</p>



<p>&#8212; Hun ble arrestert av sivilkledd politi i huset sitt da hun satte seg ved spisebordet sammen med to kollegaer for å spise lunsj, sier Kani Mohammedi, Zaras søster, i et intervju med Radio Zamaneh.  </p>



