<p><strong>Det er usikkert på hvilket grunnlag sexkjøpsloven feires for i dag står ikke kampen forbudslinjen, men om hvorvidt sexarbeid kan defineres som arbeid.</strong> </p>



<span id="more-36029"></span>



<p>I år er det ti år siden sexkjøpsloven trådte i kraft, og det ble forbudt å etterspørre og kjøpe seksuelle tjenester i Norge. I forkant av sexkjøpsloven (Str.l. §316) skjedde det en rekke endringer både i lovbestemmelser relatert til prostitusjonsfeltet og i den politiske tilnærmingen til salg og kjøp av seksuelle tjenester.&nbsp;<br></p>



<p>Første ledd i den såkalte hallikparagrafen (Str.l. §315), forbudet mot å utnytte andres prostitusjon, ble overført til menneskehandelparagrafen (Str.l. §257). Det betydde i praksis oppkriminalisering av feltet fordi mens man tidligere måtte bevise at det hadde skjedd utnyttelse av andres prostitusjon, berører hallikparagrafen slik som bestemmelsen er formulert i dag, alle former for organisering av sexarbeid inkludert samorganisering sexarbeidere imellom. Sexarbeidere kan ikke danne arbeidsfellesskap eller velge å jobbe sammen uten å risikere anmeldelse og straff. Parallelt med dette har den politiske tilnærmingen endret form og de sosialpolitiske brillene man tok på seg på begynnelsen av 1980-tallet, har i løpet av 2000-tallet blitt erstattet med kriminalpolitiske briller.&nbsp;<br></p>



<p>Det har altså skjedd både nykriminalisering og oppkriminalisering av prostitusjon så vel som endring i samfunnets holdning til og forståelse av feltet.  </p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/09/20/sexkjopsloven-10-ar-hva-na/">Sexkjøpsloven 10 år: Hva nå?</a></strong></p>



<p><strong>Politisk handlekraft og andre fernisser&nbsp;&nbsp;&nbsp;&nbsp;</strong><br>Kriminalisering av mennesker har som kjent konsekvenser utover det at et felt blir underlagt strafferettslig kontroll. Deriblant at komplekse og sammensatte sosiale fenomener reduseres til én kriminell handling. I dag er blikket rettet mot tredjepart og kunder, og parten som selger sex er redusert til et symbolsk og passivt offer for kyniske menns sexdrifter eller profittbegjær.&nbsp;&nbsp;<br></p>



<p>Kriminaliseringen sementerer i dette tilfellet også en heteronormativ kjønnsmaktforståelse basert på radikalfeministisk ideologi; kvinner selger sex, menn kjøper sex og driver businessen titulert som «horekunde» og «hallik» og/eller «menneskehandler». Det er en fortelling så spiselig at selv kristenkonservative har endret narrative fra hor som dødssynd til hor som et kjønns- og likestillingsproblem.&nbsp;<br></p>



<p>Et annet problem med kriminalisering er at et lovforbud ofte assosieres med politisk handlekraft samtidig som det er, om ikke billig, så i alle fall en enkel løsning. Sexsalg drøftes ikke som et problem relatert til fattigdom, økonomiske behov, manglende eller begrenset tilgang til arbeidsmarked, migrasjon eller rus. Ei heller at det for mange er å anse som jobb man har valgt å ha, og som noen sågar trives godt med. Våre folkevalgte viser ingen interesse for om hvordan finans- og økonomiske kriser, boligbobler og usikre arbeidsmarkeder og andre sosioøkonomiske forhold påvirker rekruttering til, og motiverer mange til å livnære seg på, sexsalg.&nbsp;<br></p>



<p>I den grad prostitusjon har vært satt på den politiske dagsorden har det nesten uten unntak vært for å foreslå skjerping av allerede eksisterende forbudsbestemmelser, eller utvide forbudsområdet.&nbsp;&nbsp;<br></p>



<p>Et tredje problem er at opprettholdelsen av forbudet blir et mål i seg selv. Det har vi også sett med narkotikaforbudet som de fleste i dag erkjenner som mislykket og skadelig, men der den politiske terskelen for å avkriminalisere likevel synes skyhøy fordi det er lite å tjene politisk på det.  </p>



<p><strong>LES OGSÅ av </strong><a href="https://radikalportal.no/author/astrid-renland/"><strong>Astrid Renland</strong></a><strong>: </strong><a href="https://radikalportal.no/2015/12/21/hvil-i-fred-galina-sandeva/"><strong>Hvil i fred, Galina Sandeva</strong></a></p>



<p><strong>Pro et contra kriminalisering   </strong><br>Det er ulike meninger om kriminalisering av sexarbeid. Forbudstilhengerne jubler og feirer at straffelovens §316 er ti år. Tyngden av forbudstilhengerne er å finne på den politiske venstresiden, og sosialister som en gang så på politiet som borgerskapets forlengede arm, regner nå statens maktapparat som sin nærmeste allierte. <br></p>



<p>De politiske partiene som gikk hardt ut mot innføring av sexkjøpsloven, og som har nedfelt i sine respektive prinsipprogram at bestemmelsene skal fjernes, har i løpet av snart to regjeringsperioder valgt å sitte så stille i som mulig i båten. Forhold som drap på sexarbeider, økt vold, lav anmeldelsesfrekvens, mer kriminalitet og rapporter som dokumenterer hvilke omkostninger kriminaliseringen har for parten som selger sex, har ikke ført til at regjeringsskuten har rugget på seg. Tvert imot, en lenge bebudet og påbegynt stortingsmelding om prostitusjon ble stoppet med et pennestrøk da regjeringspartiene møttes på Granvold, og det formidles i plattformen med samme navn, at man vil opprettholde forbudet mot sexkjøp.&nbsp;<br></p>



<p>Det er stor grunn til å tro at det var et resultat av maktspillet med Krf, selv om det ikke forklarer hvorfor kristenfolket skulle motsette seg en kunnskapsbasert prostitusjonspolitikk.&nbsp;&nbsp;<br></p>



<p>Moderpartiene til tross, Unge Høyre, Unge Venstre og FpU har alle tatt til orde for å ikke bare fjerne sexkjøpsloven, men også å få sexarbeid i ordnede former gjennom legalisering og etablering av bordeller.&nbsp;<br></p>



<p>Ser man til det sivile samfunn er motstanden mot kriminalisering stadig økende. Amnesty International og FRI &#8211; Forening for kjønns- og seksualitetsmangfold, har fattet vedtak mot kriminalisering, og alle skeive organisasjoner støtter vedtaket. HivNorge støtter fjerning av sexkjøpsloven, mens Sex og Politikk i disse dager har åpnet for debatt om hvilke ståsted og tilnærming organisasjonen skal ta og ha.&nbsp;<br></p>



<p>Motstanden mot kriminaliseringen er også stor blant menige menn og kvinner, noe som gjenspeiler seg både i mediedebatter og debatter på sosiale medier. Sexarbeid er ikke som det var for et tiår siden et smalt tema for en engere krets interesserte, som hovedsakelig besto av personer som jobbet tett på eller i feltet. Det er et politisk tema som opptar dagens kulturelle skravleklasser. <br></p>



<p>Debattene ført i pennen av konservative har uten tvil vært mer interessant enn den man finner blant såkalte radikale på venstresiden. Om man ser bort ifra Aps tenketank Progressiv, så har det vært taust fra andre av venstresidens tenketanker, som Manifest og Agenda. Den konservative tenketanken Civita publiserte i 2014 rapporten «Trenger vi en ny prostitusjonspolitikk», og har reist tematikken gjennom flere gode innlegg på tenketankens nettsted. Minerva har også engasjert seg med gode analytiske og politiske innlegg og artikler om sexarbeid, og politikken som regulerer feltet.&nbsp;&nbsp;&nbsp;</p>



<p><strong>Fra lov &#8211; til hvilken seier? </strong><br>Flere på venstresiden har som sagt feiret sexkjøpsloven. Kvinnegruppa Ottar inviterte til jubileumsfest i midten av juni, og den ble behørig kommentert av Klassekampen faste spaltister. Bortsett fra at straffelovens § 316 i seg selv regnes som en av kvinnebevegelsens viktigste seire, er det høyst usikkert på hvilket grunnlag jubelen står i taket. <br></p>



<p>Forbudet mot sexkjøp har hverken styrket politiets kamp mot internasjonal organisert kriminalitet, eller på noen måter påvirket den internasjonale handel med mennesker.&nbsp; Det var selvsagt ingen som trodde på at et forbud mot sexkjøp på nasjonalt hold skulle skape den minste krusning blant internasjonale kriminelle, som nyter godt av at migrasjon fra globale sør til globale nord er presset over på det illegale og uformelle markedet.&nbsp;<br></p>



<p>Det er vanskelig å tro at selv de innenfor Ap og SV, som forfektet at årsaken til at de konverterte fra contra til pro kriminalisering var kampen mot internasjonal kriminalitet, trodde på egen argumentasjon.&nbsp;<br></p>



<p>Når det gjelder politiets kamp mot organisert kriminalitet på hjemmebane er metodene til forveksling lik det vi i dag omtaler som totalt mislykket investering i straff. Med like stort engasjement som politiet har rettet narkotikabekjempelsen mot småkriminelle og marginaliserte stoffbrukere, raider de i dag sexarbeidere. Det er (fortsatt) forbudt å bruke dop, men å selge sex er som kjent både et høyst legalt og skatte- og avgiftspliktig erverv.&nbsp;<br></p>



<p>Siden 2015 er det politiets utlendingsseksjon, PU, som er mest aktiv i feltet. Politiet bruker samme metoder som kriminelle, og opptrer med undercover politiarbeid hvor de utgir seg for å være kunder for å få kontakt med sexarbeidere. Målet er ikke å redde kvinner fra utnyttende bakmenn, men deportere sexarbeidere.&nbsp;<br></p>



<p>Det er merkelig at det ikke skaper debatt at straffelovens bestemmelser om prostitusjon nå brukes for å oppnå politiske målkrav på utlendingsfeltet, men det gjør det altså ikke.&nbsp;&nbsp;&nbsp;<br></p>



<p>Ei heller har det endret menneskers holdning til sexkjøp eller redusert antallet som kjøper sex, og intet tyder på at det er færre som lar seg motivere til å selge sex. Sexmarkedet i Norge er mer levende enn noen gang både når det gjelder tilreisende og bofaste, og tilgangen til og etterspørselen etter betalt sex er upåklagelig og tilgjengelig i selv de minste avkrokene av landet.&nbsp;<br></p>



<p>Det eneste man har lykkes med er å gjøre det jævligere for parten som selger sex. Dagens politiske tilnærming går hardest utover de mest marginaliserte sexselgerne, deriblant ofre for menneskehandel. Noen av ofrene har sågar valgt å gå rettens vei for å få den vitnebeskyttelsen norske myndigheter har forpliktet seg til å gi til personer, som anmelder og vitner mot menneskehandlere.&nbsp;<br></p>



<p>Dette omvendte skadefølgeprinsippet der statens maktutøvelse for å hindre handlingen gjør den mer skadelig, er et problem som vi må diskutere.&nbsp;&nbsp;</p>



<p><strong>Sexarbeid er arbeid&nbsp;&nbsp;</strong><br>Jubel til tross, selv innenfor kretser som hyller straffelovens bestemmelser mot sexkjøp hersker det en økende bekymring for det de kaller en normalisering av sexarbeid. Etter ti år med sexkjøpforbud står nemlig konfliktlinjene om begrepsbruken, og begrepene man krangler om er «sexarbeid» og «sexarbeider». Man jobber med sexarbeid og man er sexarbeider.&nbsp;<br></p>



<p>Begrepene er i motsetning til de øvrige begrepene, som benyttes for å beskrive feltet og parten som selger sex, etablert, definerte og promotert av sistnevnte. Det er med andre ord sexarbeidere, og ikke storsamfunnet, som har satt den politiske dagsorden i denne debatten.&nbsp;&nbsp;<br></p>



<p>Sexarbeid er et begrep som illustrerer at salg av sex <em>er</em> arbeid og ervervsmessig virksomhet, enten man selger sex for å finansiere stoffbruk, håndtere migrantsituasjonen, forsørge familie, finansiere studentlivet eller av andre økonomiske behov og ønsker.&nbsp; Å være sexarbeider synliggjør hva man <em>gjør</em> i motsetning til begrepet «prostituert» som forteller noe om hvem man <em>er</em>, i likhet med de enda mindre flatterende termene som brukes om kvinner som selger sex, deriblant <em>hore</em>. Og sist, men ikke minst, er det både et kjønnsnøytralt og inkluderende begrep som favner sexmarkedets mangfoldighet av han, hun og hen og ulike former for sex- og erotiskarbeid.&nbsp;&nbsp;&nbsp;<br></p>



<p>Motstandere av begrepet sexarbeid vil opprettholde status quo, hvilket i praksis betyr stigma og sosial ekskludering av mennesker som selger sex. Normalisering betegner nemlig også marginaliserte gruppers rett og mulighet til å leve et normalt liv. Det ruspolitiske prosjektet har for eksempel i mange år jobbet for normalisering av stoffbrukerne gjennom å endre definisjon av stoffbruk så vel som stoffbrukerne. En gang var de umoralske narkomane og kyniske kriminelle, i dag omtales de som pasienter og brukere. Man har sågar tatt et skritt i retning av avkriminalisering av bruk og brukere. <br></p>



<p>På samme måte impliserer sexarbeider-begrepet kravet om reformer og forslag til nye politiske tilnærminger. Ikke minst innebærer begrepet at blikket rettes mot parten som selger sex og deres behov for rettigheter og trygge arbeidsforhold.&nbsp;&nbsp;&nbsp;<br></p>



<p>Når sexarbeid er avkriminalisert kan sexarbeidere få tilgang til arbeidsrettigheter. Det betyr ikke at <em>alle</em> som selger sex vil formalisere virksomheten og betale skatt og avgifter med glede fordi det nå vil gi rett til sosiale goder og ytelser, men dem som vil det kan det. Begrepet omfavner ikke bare de som definerer seg om arbeidere, en rettighetsbasert politisk tilnærming har vist seg bedre for parten som livnærer seg på sexsalg enn bruk av strafferettslig kontroll og virkemidler.&nbsp;<br></p>



<p>Utgangspunktet for debatten må være det som har minst omkostninger for parten som selger sex. Man blir ikke sexarbeider fordi kunder etterspør tjenester. Man selger sex av ulike økonomiske årsaker og behov. Å marginalisere sexarbeidere har ikke ført til noe annet enn at det har blitt mindre trygt å selge sex.&nbsp;<br></p>



<p>Ønsker man at færrest mulig skal jobbe med sexarbeid, må det det gjøres noe med de samfunnsmessige forholdene som gjør at sexsalg for mange er en nødvendig kilde til inntekt.</p>
