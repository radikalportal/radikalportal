<p><strong>Kommer sjokket grunnet kunnskapsløs kritikk mot læreplanen eller mot at noen i skolen skulle kunne la seg inspirere av marxismen?</strong></p>



<span id="more-36065"></span>



<p>I <a href="https://www.dagsavisen.no/debatt/det-rode-dyp-i-det-bla-farvannet-1.1566482">Dagsavisen 12. august</a> kommer pedagogene Glenn-Egil Torgersen og Herner Sæverot med en krass kritikk av den reviderte utgaven av læreplanen som skal iverksettes i 2020. Kritikken retter seg mot det de betegner som en snikinnføring i marxisme, hvor den pedagogiske forståelsen av dybdelæring er lagt til side til fordel for en marxistisk, og dermed politisk forståelse av dybdelæring. De tre fagdidaktikerne fra Instituttet for lærerutdanning og skoleforskning (ILS) Marte Blikstad-Balas, Peter Aashamar og Sverre Tveit <a href="https://www.dagsavisen.no/debatt/elleville-konspirasjonsteorier-1.1573165">tok 25. august til motmæle</a> mot dette innlegget og det de betegner som elleville konspirasjonsteorier. Som de skriver var det mange pedagoger og fagdidaktikere som satte kaffen i halsen da de leste “dommedagsprofetiene” i Dagsavisen 12. august. Etter å ha lest begge innleggene sitter jeg igjen med en del spørsmål, men det er spesielt ett jeg ønsker å diskutere videre, nemlig hva som var den egentlige grunnen til at fagdidaktikerne og pedagogene ved ILS satte kaffen i vrangstrupen. Var det den åpenbart grunne og kunnskapsløse kritikken av fagfornyelsen? Eller var det av sjokket over at noen kunne påstå noe så drøyt som at læreplanen og skolen kunne la seg inspirere av marxismen? </p>



<p>Som masterstudent ved lektorprogrammet i samfunnsfag ga disse debattinnleggene meg et sterkt behov for et endelig utløp for en frustrasjon som har vokst i meg gjennom alle årene mine som student. På tross av Torgersens og Sæverots grunne, og det som fremstår som kunnskapsløse argumenter, åpner de for en diskusjon som har vært fullstendig fraværende på lektorprogrammet og i det jeg har lest av skole- og undervisningsforskning de siste årene, nemlig hvilke politiske interesser og teoretiske antakelser som ligger til grunn for skole og utdanning i dag. Slik jeg ser det har nemlig Torgersen og Sæverot rett i noen av sine påstander som gjemmer seg bak de dype konspirasjonsteoriene. Fagfornyelsen kan med sine grunnprinsipper potensielt leses som en dreining i retning av kritisk pedagogiske perspektiver, en tradisjon som faktisk har røtter i marxismen. Og ja, dette er politisk, på lik linje som at alt som skjer i den norske skolen, av undervisning, læreplanutvikling og forskning, er og alltid har vært politisk. </p>



<p><strong>LES OGSÅ: </strong><a href="https://radikalportal.no/2019/03/12/tilbake-til-fremtiden-med-stoltenbergutvalget/"><strong>Tilbake til fremtiden med Stoltenbergutvalget</strong></a></p>



<p><strong>Den kritiske pedagogikkens potensial</strong><br>Dette er også hovedpoenget til kritiske pedagoger, nemlig at skole og utdanning aldri kan forstås isolert fra sin historiske og politiske kontekst. Lev Vygotsky, som blir nevnt i begge innleggene, representerer en av de fremste tenkerne innenfor kritisk pedagogikk. Hans tanker om samhandling, direkte deltakende demokrati og kollektiv handling henter uten tvil inspirasjon fra marxistisk tenkning. Kritiske pedagoger har til felles at de ønsker å transformere samfunnet til fordel for rettferdighet, likhet, demokrati og menneskelig frihet. Videre ser de på skolen som en arena hvor slike politiske og sosiale transformasjoner kan skje. At fagfornyelsen bygger på og henter inspirasjon fra Vygotsky og andre kritiske pedagoger kan derfor forstås som en reaksjon på den urettferdige utviklingen som preger samfunnet og verden i dag, og et ønske om å endre dette.</p>



<p>Argumentasjonen som presenteres i begge innleggene i Dagsavisen er såpass grunn og løsrevet fra overordnede samfunnsanalyser, at det fremstår som åpenbart for meg at vi må gå grunnleggende til verks når vi behandler den nye læreplanen. Mitt poeng er at istedenfor å bortvise enhver påstand om at skolen driver med politisk snikinnføring av det ene eller det andre, bør vi heller spørre oss hvilken politiske kontekst vi befinner oss i, hvordan dette påvirker skolen og utdanningen, hvilke type samfunnsborgere vi utdanner i skolen i dag, og på bakgrunn av dette, hva slags samfunn vi ønsker oss. Det er dette <em>kritisk tenkning </em>og <em>evne til å se sammenhenger </em>egentlig handler om.&nbsp;</p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/01/17/laerernorm-pa-felgen/">Lærernorm på felgen</a></strong></p>



<p><strong>Det nyliberale prosjektet </strong><br>Det er ingen hemmelighet eller kontroversiell påstand at det norske samfunnet har gått stadig mer i retning av en nyliberal samfunnsmodell. Nyliberalismen bygger på teorier som mener at samfunnet bør organiseres i henhold til markedsøkonomiske prinsipper. Markedsøkonomiske prinsipper innenfor kapitalismen har ett mål for øye, og det er å skape mest mulig profitt på kortest mulig tid. For å få til dette krever det et samfunn hvor institusjoner, bedrifter og enkeltindivider opptrer som markedsaktører, med alt det innebærer av konkurranse, fragmentering og spisse albuer. Til grunn for nyliberalismen ligger filosofien om at individets frihet og velvære best kan imøtekommes gjennom privat eiendomsrett og et fritt marked, fordi alle individer er født frie med like muligheter til å utfolde seg.&nbsp;</p>



<p>For å endre et sosialdemokratisk samfunn til et samfunn bygget på markedsprinsipper krever det åpenbart en helomvending av utdanningsinstitusjonene, slik at det utdannes samfunnsborgere som kan skape og opprettholde den ønskede samfunnsmodellen. I skolen og på utdanningsfeltet har dette blant annet gjort seg synlig gjennom et stadig større fokus på testing og vurdering, økende individualisering, effektivisering (flere elever per lærer), større fokus på “lønnsomme” fag (realfag) og nedleggelse av “ulønnsomme” fag (kreative fag), fritt skolevalg og stykkprisfinansiering, men også gjennom en stadig mer ukritisk skole og -utdanningsforskning som i liten grad forholder seg til sammenhengen mellom samfunnsstrukturer og utdanning. På lektorprogrammet har vi så vidt hørt om at det finnes noe som heter kritisk pedagogikk, og Marx er nærmest å regne som et fremmedord i skole- og utdanningsforskningen forøvrig. Dette kommer av at kritiske blikk på de store sammenhengene i kapitalismen så og si har vært fraværende i akademia under de siste tiårenes nyliberale hegemoni.&nbsp;</p>



<p><strong>LES OGSÅ: <a href="https://radikalportal.no/2019/01/16/laereropproret-i-usa-fortsetter-streik-mot-privatisering-i-los-angeles/">Læreropprøret i USA fortsetter &#8212; streik mot privatisering i Los Angeles</a></strong></p>



<p><strong>Den nye læreplanens potensiale </strong><br>Som den profilerte marxist-feministen Nancy Fraser påpeker, er det nyliberale hegemoniet i <sup>1 </sup>ferd med å forvitre. Med finanskrisen i 2009 og den stadig mer alvorlige klimakrisen, begynner nyliberalismen som løsning på problemene vi står overfor å miste legitimitet. Nyliberalismens svekkede legitimitet etterlater seg et politisk og teoretisk vakuum, hvor marxistisk inspirert kritisk tenkning igjen har begynt å vokse frem innenfor akademia og i samfunnet for øvrig. I lys av den fremvoksende kritiske tenkningen, er det jeg ønsker den nye læreplanen velkommen. Med fokus på kritisk tenkning, dybdelæring, evne til å se sammenhenger og aktiv, deltakende og demokratisk handling åpner den nye læreplanen nemlig opp for en overordnet diskusjon av samfunnet vi lever i, og for å utdanne samfunnsborgere som kan ta tak i de reelle problemene vi står overfor i dag. </p>



<p>Det fremstår derfor som et paradoks når Blikstad-Balas og gjengen på harde livet forsøker å løsrive de kritiske pedagogene fra sine politiske prosjekter, da nettopp sammenhengen mellom vitenskapen og politikken er det de kritiske pedagogene tviholder på i sitt arbeid med skole og utdanning. Det er dette jeg savner i motsvaret fra Blikstad-Balas og co. Istedenfor å benekte at fagfornyelsen har latt seg påvirke av pedagogiske perspektiver som står i teoretisk og politisk opposisjon til den utviklingen vi har sett i skolen og i samfunnet de siste tiårene, kunne de heller svart: Ja, vi ønsker en skole som utdanner samfunnsborgere som er rustet til å møte de enorme utfordringene vi og verden står overfor, og disse kan ikke løses av profittmaksimerende, individualistiske og konkurrerende borgere, men av et likestilt, rettferdig, samarbeidende og demokratisk folk, og dette er grunnen til at fagfornyelsen bygger på teorier fra kritiske pedagoger som Vygotsky. </p>



<p>Så gjenstår det å se om dette er tilfelle da, eller om dybdelæring, kritisk tenkning og evne til å se sammenhenger bare er pynteord uten politisk innhold.</p>



