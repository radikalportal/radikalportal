<p>Det samme mønsteret ser vi <a href="https://www.aftenposten.no/verden/i/QoEb7J/20-personer-drept-i-Texas-Politiet-sjekker-hoyreekstremt-manifest">hos
Aftenposten</a> som rapporterer at ulike mediekanaler snakker om at angrepet enten
etterforskes som hatkriminalitet eller terrorisme. Det <em>er</em> imidlertid ikke terrorisme eller engang <em>antatt</em> terrorisme, men det kan hende det etterforskes som det. Videre
skriver Aftenposten av George W. Bush, som de hevder er sønnen til Jeb Bush, «kalte
angrepet ‘terrorisme’ på Twitter». Nøyaktig hva Aftenposten sikter til her er
