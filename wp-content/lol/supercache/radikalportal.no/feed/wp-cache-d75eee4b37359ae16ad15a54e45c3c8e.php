<p><strong>Det er på tide at norske medier dementerer ryktene de har spredd om Julian Assanges konspirering med russiske myndigheter.</strong></p>



<span id="more-36011"></span>



<p><em>Teksten er skrevet sammen med <a href="https://radikalportal.no/author/frode-helmich-pedersen/">Frode Helmich Pedersen</a></em></p>



<p>I norske medier har interessen for siste nytt om Julian Assanges antatte konspirering med russisk etterretning og andre suspekte aktører i forbindelse med Hillary Clintons valgnederlag i 2016 vært upåklagelig. Men for vel to uker siden falt det en dom i New Yorks sørlige distriktsdomstol som tilbakeviser alle påstander om en slik sammensvergelse. Så vidt vi har kunnet registrere, er interessen for denne dommen på langt nær like stor som for det intrigespillet den avkler.  </p>



<p><strong>Et drepende dokument</strong><br>Jussen har gjerne en aura av papirknitrende retorikk og uendelige protokoller, men dette er drepende velskrevet tekst, som dessuten er dramatisk på akkurat den måten et rettsdokument faktisk kan være: Dommen plukker fra hverandre absolutt alle anklagene Den demokratiske nasjonalkomiteen (DNC) har kokt i hop om et «Russiagate» som angivelig skulle involvere ikke bare WikiLeaks, men også Donald Trump Jr. og en rekke medarbeidere i Trump-kampanjen, samt deres kumpaner.  </p>



<p>Dommeren,
John G. Koeltl, har hatt et stort og komplisert materiale å bryne
seg på. Først og fremst er det snakk om DNCs saksanlegg, som det er
investert betydelige ressurser og prestisje i. Der hevdes det
hardnakket at Assange, Trump Jr. &amp; co. har konspirert med
russiske myndigheter for å rappe store mengder dokumenter fra DNCs
servere og fra Clinton-kampanjesjef John Podestas epostkonto. Selve
søksmålet består av tolv omstendelige punkter, som alle inneholder
alvorlige anklager. 
</p>



<p><strong>Kriminalisering av journalistikk</strong><br>Dommer Koeltl gir ikke Demokratene medhold på ett eneste punkt. For det første: Anklagene som rettes mot russiske myndigheter, ligger utenfor New York-domstolens jurisdiksjon og kan derfor ikke behandles av denne rettsinstansen. For det andre kan ikke WikiLeaks og Assange dømmes for å ha offentliggjort dokumentene: «Dersom WikiLeaks skulle kunne holdes strafferettslig ansvarlig for å ha publisert dokumenter som dreier seg om DNCs politiske, økonomiske og velgerorienterte strategier bare fordi DNC karakteriserer dem som ‘hemmelige’ […], så vil det samme kunne skje med en hvilken som helst avis eller medieplattform.» Det ville i praksis være en kriminalisering av kritisk journalistikk og et brudd på det første grunnlovstillegget.</p>



<p>Derfor
har DNC har heller ingen grunn til å påberope seg den spesielle
lovgivingen som er knyttet til forretningshemmeligheter, for i dette
tilfellet er det snakk om opplysninger av stor offentlig interesse.
Lekkasjene «ga amerikanske velgere et gløtt inn i kulissene til et
av USAs to største politiske partier under et presidentvalg», heter
det i dommen. Konklusjonen er at alle DNCs argumenter «enten er
omtvistet eller verdiløse». Var det noen som sa
«konspirasjonsteorier»?</p>



<p><strong>Taushet i norsk presse</strong><br>Dette er altså de samme spekulasjonene som har florert i internasjonale medier, klekket ut av DNC, legitimert av deres allierte i amerikansk etterretning, og båret frem av The Guardian og en rekke andre mediehus – inkludert BT og andre norske aviser.  </p>



<p>
Vi
synes derfor det er både påfallende og beklagelig at dommen som
nådeløst punkterer de luftige spekulasjonene som offentligheten i
en årrekke er blitt bombardert med, knapt er blitt omtalt i norsk
presse. I skrivende stund, tre uker etter at dommen falt, har ingen
av Schibsted-avisene (VG, Aftenposten, BT m.fl.) omtalt den;
Dagbladet og Dagsavisen ikke heller.</p>



<p><strong>Mueller-rapporten</strong><br>Dommen kan sees i sammenheng med Mueller-rapporten, som ble offentliggjort i april i år, og som slo fast at det ikke hadde funnet sted noen konspirasjon mellom Trump-leiren og russisk etterretning under presidentvalget i 2016. Mueller hadde utstrakte fullmakter og et stort apparat til sin disposisjon, og konklusjonen var klar: «Undersøkelsene har ikke funnet bevis for at noen amerikansk borger med vitende og vilje har samarbeidet med IRAs innblandings-operasjon.» Mueller-rapporten inneholder også en omfattende gjennomgang av aktiviteten til Trumps tidligere valgkampleder, den straffedømte Paul Manafort, men uten å antyde at Manafort på noe tidspunkt møtte Julian Assange i den ecuadorianske ambassaden – slik avisen The Guardian har hevdet i en sak som fremdeles ikke er dementert.  </p>



<p><strong>Fire nye år med Trump?</strong><br>Det er mildt sagt et tankekors at både demokratene og deres sympatisører i amerikanske nyhetsmedier har hatt «Russiagate» som sin primære strategi mot Trump i over tre år. Hele dette felttoget fremstår nå som totalt forfeilet. Det synes åpenbart at DNC og de liberale mediene i USA dermed har bidratt til å styrke Trumps sjanser til fire nye år i presidentstolen. Og det er ironisk, tatt i betraktning det enorme raseriet Assange vakte blant liberalere og på deler av venstresiden fordi han angivelig i samarbeid med russisk etterretning skulle ha hjulpet Trump til å bli USAs 45. president.  </p>



<p><strong>Rettferdighet for Assange</strong><br>Man skulle tro at tiden nå var inne for selvransakelse, men foreløpig har det vært ganske taust. Vi håper likevel at norske journalister og kommentatorer vil ha integritet nok til å trekke tilbake sine falske anklager og misvisende påstander om Assange, Wikileaks og «Russiagate» i tiden som kommer. Vi minner om at Julian Assange fremdeles sitter i fengsel og at han etter alt å dømme går en dyster fremtid i møte. Uten rettferdighet for Assange, finnes det ingen reell pressefrihet i vår del av verden.  </p>
