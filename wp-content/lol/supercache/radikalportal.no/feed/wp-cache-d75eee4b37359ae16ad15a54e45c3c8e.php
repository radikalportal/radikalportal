<p>Selv ikke dagen etter omtalte norsk media hendelsen som terror. Tidlig på søndag ble det publisert <a href="https://www.nrk.no/urix/mistanke-om-at-massakren-var-rasistisk-motivert-1.14648403">en sak</a> på NRK med overskriften: «Tror kjøpesenter massakren var rasistisk motivert». NRK beskriver her at gjerningsmannen har et eksplisitt politisk motiv – rasisme – allikevel unngår de å beskrive hendelsen som terror. Det er ikke engang antatt terror, det er en massakre. Paradoksalt i en sak hvor en av underoverskriftene er «Etterforskes som terror». Det <em>er</em> imidlertid ikke terror, det <em>bare</em> etterforskes som det.</p>



