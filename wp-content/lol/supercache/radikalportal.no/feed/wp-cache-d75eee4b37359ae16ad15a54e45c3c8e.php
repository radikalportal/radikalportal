<p><strong>Folk er Folks redaktør forsøker å sno seg unna holocaustfornektelse og mener at kritikere er mot ytringsfriheten. Det er på tide at han avklarer en gang for alle hva han mener om holocaust.</strong></p>



<span id="more-35988"></span>



<p>I juli kom gatemagasinet Folk er Folk (FeF) med en utgave hvor de tilbydde Stopp Islamiseringen av Norge (SIAN), Folkebevegelsen Mot Innvandring (FMI) og mikropartiet Alliansen spalteplass. I samme utgave skrev redaktør Bjønnulv Evenrud på lederplass om hvordan dødstallene i Auschwitz hadde blitt nedjustert, med henvisning til holocaustfornekteren <a href="https://en.wikipedia.org/wiki/Robert_Faurisson">Robert Faurisson</a>. </p>



<p>Kritikken lot seg ikke vente på. Siden jeg var en av personen som startet FeF sammen med Bjønnulv skrev jeg <a href="https://radikalportal.no/2019/07/11/folk-er-folks-redaktor-legger-fattige-romfolk-pa-hoggestabben/">en kritikk</a> av linja redaktøren la seg på. Også John Færseth gikk ut i <a href="https://www.dagbladet.no/kultur/folk-er-folks-bum-fight/71388407">Dagbladet</a> og kritiserte påstandene Bjønnulv kom med. <a href="https://www.aftenposten.no/meninger/leder/i/9vxjJ5/Aftenposten-mener-Magasinet-Folk-er-folk-misbruker-romfolk">Aftenposten</a> kritiserte han på lederplass. <a href="https://filternyheter.no/hva-romkvinner-reagerer-pa-at-bladet-de-selger-fronter-holocaust-revisjonisme/">Filter Nyheter</a> dokumenterte reaksjonen til selgerne, som ikke visste hva bladet de solgte tok til orde for. Bjønnulv selv lot seg imidlertid vente på.</p>



